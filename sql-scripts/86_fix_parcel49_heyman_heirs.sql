-- 🎯 מטרה: לתקן שגיאה בסקריפט 85 — קישור יורשי הימן/היימן
--          גם בחלקה 49 (סקריפט 85 כיסה רק חלקות 13 + 31).
--
-- 📜 רקע: בנסח טאבו 49 כנראה יש את אותם 3 יורשים (1/48 כל אחד = 5.76 מ"ר).
--    הימן רן ישראל (003631074) - חלקה 49 - 5.76 מ"ר
--    הימן דוד (051194918) - חלקה 49 - 5.76 מ"ר
--    היימן קלרה (014777221) - חלקה 49 - 5.76 מ"ר (אם קיימת ב-DB)
--
-- 🧮 אימות: 3 × 5.76 = 17.28 ≈ 17.27 (שטח תמר היימן בחלקה 49) ✅
-- ============================================================


BEGIN;


-- ============================================================
-- שאילתה 1: לפני
-- ============================================================
SELECT '📸 לפני' AS "שלב", s.owner_name AS "שם", s.id_number AS "ת.ז.",
       s.parcel AS "חלקה", s.agreement_area AS "שטח", s.is_active AS "פעיל?",
       COALESCE(s.master_2015_status, '(NULL)') AS "סטטוס",
       COALESCE(s.inherited_from_id_number, '(NULL)') AS "ירש מ-"
FROM public.signed_owners s
WHERE s.id_number IN ('003631074', '051194918', '014777221')
  AND s.parcel = 49
ORDER BY s.id_number;


-- ============================================================
-- שאילתה 2: עדכון יורשי תמר היימן בחלקה 49
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'successor_to_2015_signer',
  inherited_from_id_number = '000156320',  -- תמר היימן ז"ל
  master_2015_notes = COALESCE(master_2015_notes, '') ||
    CASE
      WHEN COALESCE(master_2015_notes, '') = '' THEN ''
      ELSE ' | '
    END ||
    'ירש/ה מ-תמר היימן ז"ל (ת.ז. 000156320). חלקו 1/3 בחלקה 49 (לפי נסח טאבו).'
WHERE id_number IN ('003631074', '051194918', '014777221')
  AND parcel = 49
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 3: אחרי
-- ============================================================
SELECT '✅ אחרי' AS "שלב", s.owner_name AS "שם", s.id_number AS "ת.ז.",
       s.parcel AS "חלקה", s.agreement_area AS "שטח", s.is_active AS "פעיל?",
       s.master_2015_status AS "סטטוס", s.inherited_from_id_number AS "ירש מ-"
FROM public.signed_owners s
WHERE s.id_number IN ('003631074', '051194918', '014777221')
  AND s.parcel = 49
ORDER BY s.id_number;


COMMIT;
