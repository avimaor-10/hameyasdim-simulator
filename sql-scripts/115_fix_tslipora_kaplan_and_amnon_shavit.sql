-- מטרה: תיקון כפילויות אמנון שביט (ילובסקי) + ציפורה קפלן (ילובסקי)
--        לפי נסחי הטאבו של חלקות 28 + 51 (גוש 3852).
--
-- 🪞 כלל יסוד (זיכרון 17/05/2026):
--   "תזכור תמיד כי המידע הכי מדוייק לגבי רישום השטחים נמצא בנסח הטאבו"
--   "תרשום במדויק על פי הנסח"
--
-- 🎯 אופציה א' (אישור משתמש 17/05/2026 ערב):
--   רשומה אחת מאוחדת לכל אדם לכל חלקה (לא לפי שטרים נפרדים).
--
-- ⚠ אישור משתמש 17/05/2026 ערב:
--   "בשלב זה אל תאחד את המשפחות לא בטוח שהם מעוניינות למרות שהם
--    אותו שבט" → לא לקשר את אמנון וצפורה למשפחת ילובסקי / לקבוצה אחרת.
--   family_group_id נשאר NULL.
--
-- ============================================================
-- 📜 נתוני הנסח (נסחי טאבו עדכניים מ-11/10/2021):
--
-- חלקה 28 (27,335 מ"ר) — ציפורה בלבד (אמנון לא רשום בה):
--   • שטר 15576/1971 ירושה — 216/10800     = 546.70 מ"ר
--   • שטר 15576/1971 ירושה — 6696/324000   = 564.92 מ"ר
--   • שטר 14470/1997/1 ירושה — 3/100        = 820.05 מ"ר
--   • סה"כ ציפורה בחלקה 28:                 = 1,931.67 מ"ר
--
-- חלקה 51 (24,918 מ"ר) — אמנון + ציפורה (סימטרי):
--   • שטר 3052/1978/3 פיצול — 216/10800     = 498.36 מ"ר
--   • שטר 3052/1978/3 פיצול — 6696/324000   = 514.97 מ"ר
--   • שטר 14470/1997/1 ירושה — 3/100         = 747.54 מ"ר
--   • סה"כ לכל אחד בחלקה 51:                = 1,760.87 מ"ר
--
-- ============================================================
-- 🔧 פעולות (אופציה א' — רשומה מאוחדת):
--
-- ציפורה קפלן (ילובסקי) — אחות של משה ילובסקי:
--   STEP A — 001562909 (פורמט מודרני, ת"ז מ-1997): 2 רשומות תקינות
--            (חלקה 28: 1931.67 | חלקה 51: 1760.95). רק עדכון
--            unification_area מ-NULL ל-agreement_area (היה NULL!).
--   STEP B — 000156290 (פורמט קודם): 6 רשומות שכפול שגוי
--            (3× 564.92 + 3× 498.36). סימון is_active=FALSE.
--
-- אמנון שביט (ילובסקי) — אח של משה ילובסקי, רשום רק בחלקה 51:
--   STEP C — 001562891 (פורמט מודרני, ת"ז מ-1997): רשומה אחת
--            עם 498.36 (חלק אחד מ-3). תיקון ל-agreement_area=1760.949
--            (סכום מלא, תואם לציפורה) + unification_area.
--   STEP D — 000156289 (פורמט קודם): 2 רשומות חלקיות (514.97 + 498.36).
--            חסר 747.54 של ירושת 1997. סימון is_active=FALSE.
--
-- 🔒 BEGIN ... COMMIT — בטוח להריצה.
-- 🛠 נשמר עם $note$...$note$ למניעת בעיות גרשיים עבריים.
-- ============================================================


BEGIN;


-- ============================================================
-- BEFORE: צילום מצב לפני
-- ============================================================
SELECT 'BEFORE 115' AS step,
  s.id_number, s.owner_name, s.parcel,
  ROUND(s.agreement_area::numeric, 3)   AS agreement_area,
  ROUND(s.unification_area::numeric, 3) AS unification_area,
  s.is_active, s.is_signed, s.master_2015_status
FROM public.signed_owners s
WHERE s.id_number IN (
  '001562891', '000156289',   -- אמנון שביט (ילובסקי)
  '001562909', '000156290'    -- ציפורה קפלן (ילובסקי)
)
ORDER BY s.owner_name, s.parcel, s.id_number;


-- ============================================================
-- STEP A: ציפורה — עדכון unification_area של 001562909 (היה NULL)
-- ============================================================
UPDATE public.signed_owners
SET unification_area = agreement_area,
    master_2015_notes = COALESCE(master_2015_notes, '') ||
      CASE WHEN COALESCE(master_2015_notes, '') = '' THEN '' ELSE ' | ' END ||
      $note$עדכון 17/05/2026 (סקריפט 115): unification_area עודכן מ-NULL ל-agreement_area לאחר אבחון נסחי טאבו של חלקות 28+51 (גוש 3852). רשומה זו היא הרשומה המאוחדת התקינה של ציפורה קפלן (ילובסקי), אחות של משה ילובסקי. הסכומים תואמים בדיוק לנסח: חלקה 28 = 1,931.67 מ"ר (546.70+564.92+820.05 — שלושה רישומים מ-1971+1997), חלקה 51 = 1,760.87 מ"ר (498.36+514.97+747.54 — שלושה רישומים מ-1978+1997). ת"ז 000156290 (פורמט קודם) מבוטלת באותו סקריפט STEP B (שכפול שגוי). family_group_id נשאר NULL לפי אישור משתמש 17/05/2026.$note$
WHERE id_number = '001562909';


-- ============================================================
-- STEP B: ציפורה — ביטול 6 הרשומות השגויות של 000156290
-- ============================================================
UPDATE public.signed_owners
SET is_active = FALSE,
    master_2015_notes = COALESCE(master_2015_notes, '') ||
      CASE WHEN COALESCE(master_2015_notes, '') = '' THEN '' ELSE ' | ' END ||
      $note$סומן is_active=FALSE 17/05/2026 (סקריפט 115): ת"ז 000156290 (פורמט קודם) של "קפלן (ילובסקי) צפורה". ב-DB נכנסו תחת ת"ז זו 6 רשומות בשכפול שגוי: 3 רשומות זהות של 564.920 בחלקה 28 ועוד 3 רשומות זהות של 498.360 בחלקה 51 (סה"כ 3,189.84 מ"ר). הסכומים שגויים — לפי נסח הטאבו, הרישומים האמיתיים הם 3 שטחים שונים: בחלקה 28: 546.70 + 564.92 + 820.05 = 1,931.67 מ"ר. בחלקה 51: 498.36 + 514.97 + 747.54 = 1,760.87 מ"ר. הרשומה המאוחדת המדויקת מיוצגת בת"ז 001562909 (פורמט מודרני, פעיל). לפי אופציה א' שאושרה — רשומה אחת מאוחדת לכל חלקה (לא פיצול לפי שטרים).$note$
WHERE id_number = '000156290';


-- ============================================================
-- STEP C: אמנון — עדכון 001562891 לסכום המלא בחלקה 51
-- ============================================================
UPDATE public.signed_owners
SET agreement_area   = 1760.949,
    unification_area = 1760.949,
    master_2015_notes = COALESCE(master_2015_notes, '') ||
      CASE WHEN COALESCE(master_2015_notes, '') = '' THEN '' ELSE ' | ' END ||
      $note$עדכון 17/05/2026 (סקריפט 115): agreement_area + unification_area עודכנו ל-1,760.949 (היה 498.36 — חלק אחד מ-3 בלבד). לפי נסח טאבו גוש 3852 חלקה 51, אמנון שביט (ילובסקי) רשום בשלושה רישומים: שטר 3052/1978/3 פיצול 216/10800 (498.36) + שטר 3052/1978/3 פיצול 6696/324000 (514.97) + שטר 14470/1997/1 ירושה 3/100 (747.54) = סה"כ 1,760.87 מ"ר. הערך 1,760.949 תואם לערך של אחותו ציפורה ב-001562909 (אותם חלקים מתמטית). לפי אופציה א' — רשומה מאוחדת בת"ז המודרנית, לא פיצול ל-3 רשומות נפרדות. ת"ז 000156289 (פורמט קודם) מבוטלת באותו סקריפט STEP D. family_group_id נשאר NULL לפי אישור משתמש 17/05/2026.$note$
WHERE id_number = '001562891';


-- ============================================================
-- STEP D: אמנון — ביטול 2 הרשומות של 000156289
-- ============================================================
UPDATE public.signed_owners
SET is_active = FALSE,
    master_2015_notes = COALESCE(master_2015_notes, '') ||
      CASE WHEN COALESCE(master_2015_notes, '') = '' THEN '' ELSE ' | ' END ||
      $note$סומן is_active=FALSE 17/05/2026 (סקריפט 115): ת"ז 000156289 (פורמט קודם) של "שביט (ילובסקי) אמנון". ב-DB נכנסו תחת ת"ז זו 2 רשומות חלקיות בחלקה 51 (514.97 + 498.36 = 1,013.33 מ"ר) — אלה רק 2 מ-3 רישומי הנסח, וחסר רישום הירושה של 1997 (747.54 מ"ר משטר 14470/1997/1, חלק 3/100). הסכום המלא 1,760.949 מ"ר מיוצג נכון בת"ז 001562891 (פורמט מודרני, פעיל) — סקריפט 115 STEP C. לפי אופציה א' שאושרה — רשומה אחת מאוחדת.$note$
WHERE id_number = '000156289';


-- ============================================================
-- AFTER: וידוא התיקון
-- ============================================================
SELECT 'AFTER 115' AS step,
  s.id_number, s.owner_name, s.parcel,
  ROUND(s.agreement_area::numeric, 3)   AS agreement_area,
  ROUND(s.unification_area::numeric, 3) AS unification_area,
  s.is_active, s.is_signed, s.master_2015_status
FROM public.signed_owners s
WHERE s.id_number IN (
  '001562891', '000156289',
  '001562909', '000156290'
)
ORDER BY s.owner_name, s.parcel, s.id_number;


-- ============================================================
-- SUMMARY: רק רשומות פעילות אחרי התיקון
-- ============================================================
SELECT 'SUMMARY 115 ACTIVE' AS step,
  s.id_number, s.owner_name, s.parcel,
  ROUND(s.agreement_area::numeric, 3)   AS agreement_area,
  ROUND(s.unification_area::numeric, 3) AS unification_area
FROM public.signed_owners s
WHERE s.id_number IN (
  '001562891', '000156289',
  '001562909', '000156290'
)
  AND s.is_active = TRUE
ORDER BY s.owner_name, s.parcel;


COMMIT;


