-- מטרה: עדכון פלדמן אהרון ז"ל — קישור למשפחת פלדמן ברקוביץ +
--        סטטוס + הערה היסטורית מקיפה.
--
-- 🔑 תובנה חדשה (אישור משתמש 17/05/2026 ערב):
--   פלדמן אהרון נפטר ב-1999 אך הצוואה שלו לא בוצעה רשמית בטאבו עד היום.
--   לפי יוסף פלדמן (מקור משפחתי) — אהרון הוריש את חלקו ליורשי משפחת
--   פלדמן (יוסף, רחל, ועוד). הירושה תירשם בעתיד.
--
-- 📜 רקע מנסחי טאבו:
--   נסחים (חלקות 13 + 49):
--   • שטר 29339/1997/1 (ירושה מפלדמן יעקב הסב 002716926):
--     - בחלקה 13: פלדמן אהרן (156138) — 1/8 = 37.75 מ"ר
--     - בחלקה 49: פלדמן אהרן (156138) — 1/16 = 69.0625 מ"ר
--   • שטר 29339/1997/4 (תיקון טעות סופר, 1997):
--     - בחלקה 49: פלדמן אהרון (001561380) — 1/8 = 138.125 מ"ר
--   • סה"כ אהרון: 244.9375 מ"ר
--
-- ✅ 3 אקסלים מאשרים: "חתום הסכם מקור" + "לקוח מקור"
--   • רשימת בעלי קרקע מקור ופרטי התקשרות
--   • דוח_אי_התאמה_בעלים
--   • טבלת בעלי זכויות מהמנדיי מאומת נסח טאבו מרץ 26
--   (החתימה בוצעה דרך היורשים אחרי מות אהרון)
--
-- 🪞 עיקרון "הסכם הניהול עוקב אחר רישום הטאבו":
--   כל עוד אהרון רשום פעיל בנסח (גם לאחר מותו ב-1999, כי הירושה
--   לא נרשמה), שטחו 244.9375 מ"ר נכלל באיחוד וחלוקה.
--
-- ⚠ אהרון אינו ברשימת התובעים בבוררות (נספח א'), בעוד שאחיו ואחיינו
--   כן ברשימה. הסיבה: ז"ל ולא יכול להיות צד; היורשים שיירשמו בעתיד
--   יהיו הצד.
--
-- 🌳 משפחה: אהרון בן של פלדמן יעקב הסב (002716926). אחים: מרדכי (ז"ל),
--   זית-טוב לאה (ז"ל), פלדמן יוחנן (ז"ל). אחיינים: פלדמן דוד,
--   ברקוביץ רחל, פלדמן יוסף (ילדי יוחנן), פלדמן אמנון (דרך מרדכי→שרה).
--
-- 🔒 BEGIN ... COMMIT — בטוח להריצה.
-- 🛠 נשמר עם $note$...$note$ כדי למנוע בעיות עם גרשיים בטקסט עברי.
-- ============================================================


BEGIN;


-- ============================================================
-- BEFORE: צילום מצב לפני
-- ============================================================
SELECT
  'BEFORE 111' AS step,
  s.id_number,
  s.owner_name,
  s.parcel,
  ROUND(s.agreement_area::numeric, 3)   AS agreement_area,
  ROUND(s.unification_area::numeric, 3) AS unification_area,
  s.is_active,
  s.is_signed,
  s.master_2015_status,
  s.family_group_id,
  s.inherited_from_id_number
FROM public.signed_owners s
WHERE s.id_number IN ('001561380', '000156138')
ORDER BY s.parcel, s.id_number;


-- ============================================================
-- STEP A: ודא שקבוצת "פלדמן ברקוביץ" קיימת
-- ============================================================
INSERT INTO public.family_groups (family_name, is_active)
SELECT 'פלדמן ברקוביץ', TRUE
WHERE NOT EXISTS (
  SELECT 1 FROM public.family_groups
  WHERE family_name = 'פלדמן ברקוביץ'
);


-- ============================================================
-- STEP B: עדכון פלדמן אהרון ז"ל (000156138) — חלקות 13 + 49
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status       = 'verified',
  inherited_from_id_number = '002716926',
  family_group_id          = (SELECT id FROM public.family_groups WHERE family_name = 'פלדמן ברקוביץ'),
  master_2015_notes        = $note$פלדמן אהרון ז"ל (נפטר 1999) — בן של פלדמן יעקב הסב (002716926). הצוואה לא בוצעה רשמית בטאבו עד היום. לפי יוסף פלדמן (מקור משפחתי), אהרון הוריש את חלקו ליורשי משפחת פלדמן: יוסף, רחל, ועוד. כשהירושה תירשם בטאבו — תופיע גם ב-DB. עיקרון: "הסכם הניהול עוקב אחר רישום הטאבו" — שטחו 244.94 מ"ר נכלל באיחוד וחלוקה. ירש מיעקב ב-27/11/1997 (שטר 29339/1997/1): 1/8 בחלקה 13 (37.75 מ"ר) + 1/16 בחלקה 49 (69.06 מ"ר). יש ת"ז נוספת 001561380 בחלקה 49 (תיקון טעות סופר, 138.125 מ"ר). חתום הסכם מקור: מאומת ב-3 אקסלים (חתימה דרך יורשיו אחרי מותו). אינו ברשימת התובעים בבוררות — סביר כי הוא ז"ל. דורש בדיקה פיזית בקלסרים: לאתר צוואה + רשימת יורשים. תיעוד 17/05/2026 (סקריפט 111).$note$
WHERE id_number = '000156138';


-- ============================================================
-- STEP C: עדכון פלדמן אהרון ז"ל (001561380) — חלקה 49
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status       = 'verified',
  inherited_from_id_number = '002716926',
  family_group_id          = (SELECT id FROM public.family_groups WHERE family_name = 'פלדמן ברקוביץ'),
  master_2015_notes        = $note$פלדמן אהרון ז"ל (נפטר 1999) — ת"ז זו (001561380) היא של "תיקון טעות סופר" בשטר 29339/1997/4 בחלקה 49 (138.125 מ"ר, 1/8). יש רשומה נוספת בת"ז 000156138 (פורמט 156138 ישן) בחלקות 13 + 49. אותו אדם. הצוואה לא בוצעה רשמית בטאבו. לפי יוסף פלדמן: יורשים = יוסף, רחל, ועוד. הקרקע תעבור בעת רישום ירושה עתידי. מוריש: פלדמן יעקב הסב (002716926). חתום הסכם מקור: מאומת ב-3 אקסלים (דרך היורשים). אינו בבוררות — ז"ל. דורש בדיקה פיזית בקלסרים. תיעוד 17/05/2026 (סקריפט 111).$note$
WHERE id_number = '001561380';


-- ============================================================
-- AFTER: וידוא התיקון
-- ============================================================
SELECT
  'AFTER 111' AS step,
  s.id_number,
  s.owner_name,
  s.parcel,
  ROUND(s.agreement_area::numeric, 3)   AS agreement_area,
  s.is_active,
  s.is_signed,
  s.master_2015_status,
  s.inherited_from_id_number,
  fg.family_name
FROM public.signed_owners s
LEFT JOIN public.family_groups fg ON fg.id = s.family_group_id
WHERE s.id_number IN ('001561380', '000156138')
ORDER BY s.parcel, s.id_number;


-- ============================================================
-- SUMMARY: סיכום משפחת פלדמן ברקוביץ אחרי הקישור
-- ============================================================
SELECT
  'FAMILY SUMMARY' AS step,
  COUNT(DISTINCT s.id_number) AS unique_ids,
  COUNT(*) AS total_records,
  ROUND(SUM(s.agreement_area)::numeric, 3) AS total_area
FROM public.signed_owners s
JOIN public.family_groups fg ON fg.id = s.family_group_id
WHERE fg.family_name = 'פלדמן ברקוביץ'
  AND s.is_active = TRUE;


COMMIT;


