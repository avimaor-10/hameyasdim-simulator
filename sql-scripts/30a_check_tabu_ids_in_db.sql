-- מטרה: לפני הוספת חסרים — לבדוק איזה ת"ז מנסחי טאבו מאי 2026
-- כבר קיימים ב-`signed_owners` ואיזה לא, לחלקות הרלוונטיות.
--
-- הת"ז נאספו מנסחי טאבו ע"י pdftotext על הקבצים:
--   נסחי טאבו מאי 2026/3852-{6,28,31,44,49,51,68}.pdf
--
-- מטרת הסקריפט: לזהות איזה בעלים בנסח אמור להיות חתום מקור 2015,
-- אך לא מופיע ב-DB — אלו "חסרים אמיתיים" שצריך להוסיף.
-- ============================================================

WITH tabu_ids AS (
  SELECT * FROM (VALUES
    -- חלקה 31 (חלקה של מיכאלי עמרי + פיינשטין לימור)
    ('054133095', 31, 'מיכאלי מיקי (יורש עמרי)'),
    ('057412058', 31, 'פיינשטיין לימור'),

    -- חלקה 49 (גם פיינשטין לימור + הימן תמר)
    ('057412058', 49, 'פיינשטיין לימור'),

    -- חלקה 68 (דניאלי לביא — רק 2 ת"ז שם!)
    ('032250805', 68, 'אחד הבעלים בחלקה 68 - אולי דניאלי לביא?'),
    ('033155409', 68, 'אחד הבעלים בחלקה 68 - אולי דניאלי לביא?'),

    -- חלקה 44 (פרסטנפלד יהודה, פרסטנפלד תמר, ראובן עמוס — 22 ת"ז)
    ('008286072', 44, 'בעלים חלקה 44'),
    ('050344290', 44, 'בעלים חלקה 44'),
    ('005468658', 44, 'בעלים חלקה 44'),
    ('017310509', 44, 'בעלים חלקה 44'),
    ('017310491', 44, 'בעלים חלקה 44'),
    ('027967280', 44, 'בעלים חלקה 44'),
    ('025610460', 44, 'בעלים חלקה 44'),
    ('024032013', 44, 'בעלים חלקה 44'),
    ('057412272', 44, 'בעלים חלקה 44'),
    ('029643699', 44, 'בעלים חלקה 44'),
    ('056782469', 44, 'בעלים חלקה 44'),
    ('005136299', 44, 'בעלים חלקה 44'),
    ('005656922', 44, 'בעלים חלקה 44'),
    ('055077226', 44, 'בעלים חלקה 44'),
    ('057151649', 44, 'בעלים חלקה 44'),
    ('022569578', 44, 'בעלים חלקה 44'),
    ('305685489', 44, 'בעלים חלקה 44'),
    ('200781490', 44, 'בעלים חלקה 44'),
    ('303737662', 44, 'בעלים חלקה 44'),
    ('520029281', 44, 'בעלים חלקה 44 (חברה)')
  ) AS t(id_number, parcel, source_note)
)
SELECT
  ti.id_number AS "ת_ז_מנסח",
  ti.parcel AS "חלקה_בנסח",
  ti.source_note AS "הערה_מהנסח",
  COALESCE(s.owner_name, '🔴 לא קיים ב-DB') AS "שם_ב_DB",
  COALESCE(s.master_2015_status, '—') AS "מקור_2015",
  COALESCE(s.ownership_category, '—') AS "קטגוריה",
  s.is_active AS "פעיל",
  COUNT(s.id) OVER (PARTITION BY ti.id_number) AS "מספר_רשומות_ב_DB"
FROM tabu_ids ti
LEFT JOIN public.signed_owners s ON s.id_number = ti.id_number
ORDER BY ti.parcel, ti.id_number, s.id;


-- ============================================================
-- שאילתה משלימה — רשומות פיזיות בחלקות 6, 28, 44, 51, 68 לפי שם
-- ============================================================
-- מציגה את מי שיש ב-DB עם חלקה רלוונטית, כדי לבדוק אם איזה
-- מהחסרים האמיתיים בעצם רשום עם שם שלא ציפינו
SELECT
  parcel AS "חלקה",
  owner_name AS "שם",
  id_number AS "ת_ז",
  ROUND(agreement_area::numeric) AS "שטח",
  master_2015_status AS "מקור",
  ownership_category AS "קטגוריה",
  is_active AS "פעיל"
FROM public.signed_owners
WHERE parcel IN (6, 28, 44, 51, 68)
  AND (
    owner_name LIKE '%מורי%'
    OR owner_name LIKE '%זלץ%'
    OR owner_name LIKE '%פרסטנפלד%'
    OR owner_name LIKE '%ראובן%'
    OR owner_name LIKE '%שילוח%'
    OR owner_name LIKE '%דניאלי%'
    OR owner_name LIKE '%מיכאלי%'
    OR owner_name LIKE '%פינשטיין%'
    OR owner_name LIKE '%פיינשטין%'
  )
ORDER BY parcel, owner_name;
