-- דוח 4 — סיכום הקרקע: 350 דונם של 2015 → איפה היא היום?
-- ============================================================

SELECT
  CASE
    WHEN s.is_active = TRUE AND fg.family_name IS NOT NULL THEN '🟢 פעיל בקבוצה: ' || fg.family_name
    WHEN s.is_active = TRUE AND fg.family_name IS NULL THEN '🟢 פעיל ללא קבוצה'
    WHEN s.is_active = FALSE THEN '⚫ לא-פעיל (היסטוריה / מכר)'
  END AS category,
  COUNT(*) AS records,
  COUNT(DISTINCT s.id_number) AS unique_owners,
  ROUND(SUM(s.agreement_area)::numeric, 0) AS total_area_sqm,
  ROUND((SUM(s.agreement_area)/1000)::numeric, 1) AS total_dunams
FROM public.signed_owners s
LEFT JOIN public.family_groups fg ON fg.id = s.family_group_id
GROUP BY category
ORDER BY total_area_sqm DESC;
