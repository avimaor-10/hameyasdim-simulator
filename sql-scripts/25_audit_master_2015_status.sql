db_state AS (
  SELECT
    s.owner_name,
    BOOL_OR(s.is_active) AS has_active,
    BOOL_OR(NOT s.is_active) AS has_inactive,
    COUNT(*) AS records_count,
    ROUND(SUM(s.agreement_area)::numeric, 2) AS total_area
  FROM public.signed_owners s
  GROUP BY s.owner_name
),
heirs_per_master AS (
  -- יורשים — בעלים שיש להם predecessor_id_number שמצביע על חתום 2015
  SELECT
    pred.owner_name AS predecessor_name,
    COUNT(DISTINCT heir.id_number) AS heirs_count,
    BOOL_OR(heir.is_active) AS has_active_heir
  FROM public.signed_owners heir
  JOIN public.signed_owners pred ON pred.id_number = heir.predecessor_id_number
  WHERE heir.is_active = TRUE
  GROUP BY pred.owner_name
)
SELECT
  m.name AS master_name,
  m.parcels AS expected_parcels,
  CASE
    WHEN db.has_active = TRUE THEN '🟢 פעיל ב-DB'
    WHEN db.has_inactive = TRUE AND h.has_active_heir = TRUE THEN '🟡 לא פעיל, יורש פעיל'
    WHEN db.has_inactive = TRUE THEN '🟠 לא פעיל, אין יורש'
    WHEN h.has_active_heir = TRUE THEN '🟡 רק יורש פעיל (לא ב-DB עצמו)'
    ELSE '🔴 חסר לחלוטין!'
  END AS status,
  COALESCE(db.records_count, 0) AS db_records,
  COALESCE(db.total_area, 0) AS db_area,
  COALESCE(h.heirs_count, 0) AS active_heirs
FROM master_2015 m
LEFT JOIN db_state db ON db.owner_name ILIKE '%' || m.name || '%'
                     OR m.name ILIKE '%' || db.owner_name || '%'
LEFT JOIN heirs_per_master h ON h.predecessor_name ILIKE '%' || m.name || '%'
                              OR m.name ILIKE '%' || h.predecessor_name || '%'
ORDER BY
  CASE
    WHEN db.has_active = TRUE THEN 1
    WHEN h.has_active_heir = TRUE THEN 2
    WHEN db.has_inactive = TRUE THEN 3
    ELSE 4
  END,
  m.name;


