--          ושסכום ה"איחוד" לא יהיה גדול מהרשום.
--
-- 📐 לפני:
--   • agreement_area = 6,185 (רשום בהסכם 2015)
--   • unification_area = 8,171.46 (override פעיל)
--   • באיחוד תוצג: 8,171 → גורם לקבוצה להציג 15,686 (יותר מ-13,699 רשום)
--
-- 📐 אחרי:
--   • agreement_area = 6,185 (ללא שינוי)
--   • unification_area = 6,185 (שווה לרשום → override לא יעוף)
--   • באיחוד תוצג: 6,185 → קבוצה תציג ~13,699 רשום = ~13,699 איחוד ✓
--
-- 🔒 BEGIN ... COMMIT אוטומטי (אישור מראש).
-- ============================================================


BEGIN;


-- ============================================================
-- שאילתה 1: לפני — וזוב
-- ============================================================
SELECT
  '📸 לפני' AS "שלב",
  s.owner_name AS "שם",
  s.agreement_area AS "agreement",
  s.unification_area AS "unification",
  s.unification_area - s.agreement_area AS "פער"
FROM public.signed_owners s
WHERE s.id_number = '513435180'
  AND s.is_active = TRUE;


-- ============================================================
-- שאילתה 2: UPDATE — סנכרון
-- ============================================================
UPDATE public.signed_owners
SET unification_area = agreement_area  -- 6,185
WHERE id_number = '513435180'  -- חב' וזוב נכסים בע"מ
  AND family_group_id = 'a990ed96-5b11-4d1a-9db1-89c76833af8b'
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 3: אחרי — וזוב
-- ============================================================
SELECT
  '✅ אחרי' AS "שלב",
  s.owner_name AS "שם",
  s.agreement_area AS "agreement",
  s.unification_area AS "unification",
  s.unification_area - s.agreement_area AS "פער"
FROM public.signed_owners s
WHERE s.id_number = '513435180'
  AND s.is_active = TRUE;


COMMIT;


