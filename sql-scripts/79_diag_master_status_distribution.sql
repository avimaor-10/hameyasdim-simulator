-- 🎯 מטרה: לראות את הפיזור הנוכחי של master_2015_status
--          לפני שמחליטים אילו רשומות לסמן ידנית כ"אומת".
--
-- 📚 הקשר: המשתמש ביקש "להמיר לאומת" — כלומר לסמן רשומות
--          שעברו בדיקה ידנית כ-verified. צריך לדעת:
--          1. כמה רשומות בכל סטטוס?
--          2. מי הקבוצות שעברנו עליהן (יצחקי, וזוב, דניאלי וכו')?
--          3. מה הסטטוס הנוכחי שלהן?
--
-- 🔒 SELECT בלבד. בטוח לחלוטין.
-- ============================================================


-- ============================================================
-- שאילתה 1: פיזור כללי של master_2015_status
-- ============================================================
SELECT
  COALESCE(master_2015_status, '(NULL)') AS "סטטוס",
  COUNT(*) AS "כמות",
  ROUND(COUNT(*)::numeric * 100.0 / SUM(COUNT(*)) OVER (), 1) AS "אחוז"
FROM public.signed_owners
WHERE is_active = TRUE
GROUP BY master_2015_status
ORDER BY COUNT(*) DESC;


-- ============================================================
-- שאילתה 2: פיזור הסטטוס עבור החברים בקבוצת יצחקי
-- (כל החברים שעברנו עליהם ידנית — צריכים להיות 'verified')
-- ============================================================
SELECT
  s.owner_name AS "שם",
  s.id_number AS "ת.ז./ח.פ.",
  s.parcel AS "חלקה",
  s.agreement_area AS "שטח",
  COALESCE(s.master_2015_status, '(NULL)') AS "סטטוס נוכחי",
  CASE
    WHEN s.master_2015_status = 'verified' THEN '✅ כבר אומת'
    ELSE '⏸ ממתין להמרה ל-verified'
  END AS "המלצה"
FROM public.signed_owners s
WHERE s.family_group_id = 'a990ed96-5b11-4d1a-9db1-89c76833af8b'
  AND s.is_active = TRUE
ORDER BY s.parcel, s.agreement_area DESC NULLS LAST;


-- ============================================================
-- שאילתה 3: דניאלי + יורשים — סטטוס נוכחי
-- ============================================================
SELECT
  s.owner_name AS "שם",
  s.id_number AS "ת.ז.",
  s.parcel AS "חלקה",
  s.agreement_area AS "שטח",
  s.is_active AS "פעיל?",
  COALESCE(s.master_2015_status, '(NULL)') AS "סטטוס נוכחי"
FROM public.signed_owners s
WHERE s.id_number IN ('002787430', '058759978', '022728638', '024469033')
ORDER BY s.is_active DESC, s.owner_name;


-- ============================================================
-- שאילתה 4: רשומות שכבר מסומנות 'verified' (לראות מה כבר נבדק)
-- ============================================================
SELECT
  s.owner_name AS "שם",
  s.id_number AS "ת.ז./ח.פ.",
  s.parcel AS "חלקה",
  s.agreement_area AS "שטח",
  COALESCE(s.master_2015_folder, '(ריק)') AS "תיקייה",
  COALESCE(LEFT(s.master_2015_notes, 50), '(ריק)') AS "הערה (50 תווים)"
FROM public.signed_owners s
WHERE s.master_2015_status = 'verified'
  AND s.is_active = TRUE
ORDER BY s.parcel, s.owner_name
LIMIT 50;


-- ============================================================
-- 📋 צפי תוצאות:
--   שאילתה 1: יראה כמה רשומות יש בכל סטטוס (NULL/not_checked
--             יהיו ברוב, verified בכמות קטנה)
--   שאילתה 2: כל חברי קבוצת יצחקי (7 ישויות) — סטטוסים נוכחיים
--   שאילתה 3: יהודה דניאלי + 3 יורשים
--   שאילתה 4: 50 הראשונות שכבר 'verified' (כדי לראות מה היה הסטנדרט)
--
-- ➡ אחרי הריצה תחליט אם להריץ סקריפט שמסמן ידנית את הרשימה הספציפית.
