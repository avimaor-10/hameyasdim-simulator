--          בחלקה 21 ל-is_active=FALSE — למרות שעדיין הבעלים הרשום בטאבו!
--
-- 📜 רקע (תובנה חדשה): "הסכם הניהול עוקב אחר רישום הטאבו".
--    אם בנסח טאבו מאי 2026 (חלקה 21) "מועלם אליהו" (4/755793)
--    עדיין רשום כבעלים (שטר 9419/1976/2, חלק 1/10),
--    אזי הירושה טרם נרשמה והוא **עדיין** מחויב להסכם הניהול
--    ושטחו 1,392.60 מ"ר נכנס לאיחוד וחלוקה.
--
-- 📐 פעולות:
--   A. החזרת רשומה 4/755793 בחלקה 21 ל-is_active=TRUE
--      (זה הפורמט המופיע בנסח — אם זה לא ת.ז. תקנית, זה השם בטאבו)
--   B. רשומה 000755793 בחלקה 21 — להשאיר FALSE + הערה "כפילות"
--      (זו רשומה שגויה ב-DB; היא לא מופיעה בנסח)
--
-- ⚠ הערה לעתיד: לפני סימון בעל קרקע נפטר כ-FALSE,
--    יש לוודא שהירושה נרשמה **באותה חלקה** בנסח טאבו.
-- ============================================================


BEGIN;


-- ============================================================
-- שאילתה 1: לפני — צילום מצב חלקה 21
-- ============================================================
SELECT '📸 לפני' AS "שלב",
  s.owner_name AS "שם",
  s.id_number AS "ת.ז.",
  s.parcel AS "חלקה",
  s.agreement_area AS "שטח",
  s.is_active AS "פעיל?",
  COALESCE(LEFT(s.master_2015_notes, 60), '') AS "הערה"
FROM public.signed_owners s
WHERE s.parcel = 21
  AND s.id_number IN ('000755793', '4/755793')
ORDER BY s.id_number;


-- ============================================================
-- צעד A: החזרת 4/755793 בחלקה 21 ל-TRUE
-- ============================================================
UPDATE public.signed_owners
SET
  is_active = TRUE,
  master_2015_notes = 'מועלם אליהו ז"ל — נפטר לפני 04/01/2026, אולם בחלקה 21 הירושה טרם נרשמה בטאבו. בנסח טאבו מאי 2026 (שטר 9419/1976/2) הוא עדיין הבעלים הרשום (1/10 = 1,392.60 מ"ר). הסכם הניהול תקף עליו. הרשומה תוסר רק לאחר רישום הירושה ליורשיו.'
WHERE id_number = '4/755793'
  AND parcel = 21;


-- ============================================================
-- צעד B: סימון 000755793 בחלקה 21 כ-"כפילות"
-- (נשאר is_active=FALSE; ההערה מסבירה למה)
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_notes = 'כפילות שגויה — בנסח טאבו של חלקה 21 (מאי 2026) הת.ז. הרשומה היא 4/755793 בלבד. רשומה זו עם ת.ז. 000755793 כנראה הוזנה בטעות. הרשומה הנכונה היא 4/755793 (עדיין פעילה). [סומן 15/05/2026]'
WHERE id_number = '000755793'
  AND parcel = 21
  AND is_active = FALSE;


-- ============================================================
-- שאילתה 2: אחרי — אימות
-- ============================================================
SELECT '✅ אחרי' AS "שלב",
  s.owner_name AS "שם",
  s.id_number AS "ת.ז.",
  s.parcel AS "חלקה",
  s.agreement_area AS "שטח",
  s.is_active AS "פעיל?",
  COALESCE(LEFT(s.master_2015_notes, 100), '') AS "הערה"
FROM public.signed_owners s
WHERE s.parcel = 21
  AND s.id_number IN ('000755793', '4/755793')
ORDER BY s.is_active DESC, s.id_number;


COMMIT;


