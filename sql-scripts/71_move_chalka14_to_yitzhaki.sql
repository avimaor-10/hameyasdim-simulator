-- 🎯 מטרה: להעביר 4 רשומות מ"ללא קבוצה" לקבוצת יצחקי
--          (חלקה 14: באבי אליהו, באבי ראובן, דסטא, מחלבות).
--
-- 🆔 קבוצת דודי יצחקי: a990ed96-5b11-4d1a-9db1-89c76833af8b
--
-- 📋 4 הרשומות (לפי ת.ז./ח.פ. מנסח טאבו):
--   • באבי אליהו        024493942 — 500 מ"ר
--   • באבי ראובן        027902063 — 500 מ"ר
--   • דסטא.ח אחזקות     515133858 — 1,245 מ"ר
--   • מחלבת המושבה      512250143 — 1,245 מ"ר
--   • סה"כ                       — 3,490 מ"ר
--
-- 🔄 מעדכן 2 שדות:
--   • family_group_id      — שכבה משפטית (דמי ייזום ספציפיים)
--   • allocation_group_id  — שכבה אופרטיבית (לשיבוץ — ברירת מחדל)
--
-- 🛡 BEGIN; ... ; -- COMMIT/ROLLBACK ידני בסוף.
-- ============================================================


BEGIN;


-- ============================================================
-- שאילתה 1: צילום מצב לפני (זיהוי לפי ת.ז.)
-- ============================================================
SELECT
  '📸 לפני UPDATE' AS "שלב",
  s.owner_name AS "שם",
  s.id_number AS "ת.ז.",
  s.parcel AS "חלקה",
  ROUND(s.agreement_area::numeric, 2) AS "שטח",
  COALESCE(fg.family_name, '⚠ ללא קבוצה') AS "קבוצה משפטית",
  s.allocation_group_id AS "קבוצת הקצאה (id)"
FROM public.signed_owners s
LEFT JOIN public.family_groups fg ON fg.id = s.family_group_id
WHERE s.parcel = 14
  AND s.is_active = TRUE
  AND s.id_number IN (
    '024493942',  -- באבי אליהו
    '027902063',  -- באבי ראובן
    '515133858',  -- דסטא.ח אחזקות
    '512250143'   -- מחלבת המושבה
  )
ORDER BY s.id_number;


-- ============================================================
-- שאילתה 2: UPDATE — העברה לקבוצת יצחקי (2 שדות)
-- ============================================================
UPDATE public.signed_owners
SET
  family_group_id     = 'a990ed96-5b11-4d1a-9db1-89c76833af8b',  -- 🟧 יצחקי
  allocation_group_id = 'a990ed96-5b11-4d1a-9db1-89c76833af8b'   -- 🟧 יצחקי
WHERE parcel = 14
  AND is_active = TRUE
  AND id_number IN (
    '024493942',
    '027902063',
    '515133858',
    '512250143'
  );


-- ============================================================
-- שאילתה 3: צילום מצב אחרי
-- ============================================================
SELECT
  '📸 אחרי UPDATE' AS "שלב",
  s.owner_name AS "שם",
  s.id_number AS "ת.ז.",
  s.parcel AS "חלקה",
  ROUND(s.agreement_area::numeric, 2) AS "שטח",
  fg.family_name AS "קבוצה משפטית חדשה",
  fg2.family_name AS "קבוצת הקצאה חדשה"
FROM public.signed_owners s
LEFT JOIN public.family_groups fg ON fg.id = s.family_group_id
LEFT JOIN public.family_groups fg2 ON fg2.id = s.allocation_group_id
WHERE s.parcel = 14
  AND s.is_active = TRUE
  AND s.id_number IN (
    '024493942',
    '027902063',
    '515133858',
    '512250143'
  )
ORDER BY s.id_number;


-- ============================================================
-- שאילתה 4: סיכום חלקה 14 אחרי ההעברה
-- ============================================================
SELECT
  '🎯 סיכום חלקה 14' AS "מדד",
  COALESCE(fg.family_name, '⚠ ללא קבוצה') AS "קבוצה",
  COUNT(s.id) AS "מס׳ חתומים",
  ROUND(SUM(s.agreement_area)::numeric, 2) AS "סך שטח"
FROM public.signed_owners s
LEFT JOIN public.family_groups fg ON fg.id = s.family_group_id
WHERE s.parcel = 14
  AND s.is_active = TRUE
GROUP BY fg.family_name
ORDER BY SUM(s.agreement_area) DESC NULLS LAST;


