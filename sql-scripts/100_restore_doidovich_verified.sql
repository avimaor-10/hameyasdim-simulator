-- 🎯 מטרה: תיקון נקודתי לאחר אימות מול 3 מקורות אמת.
--          להחזיר רק את "דוידוביץ (זיצוב) אורה" ל-verified —
--          התגלה שיש לה PDF הסכם חתום בתיקייה:
--          "ריכוז הסכמי ניהול על בעלי הקרקע/חלקה 13/אורה דוידוביץ.pdf"
--          (וגם בחלקות 16 ו-49).
--
-- 🔍 רקע: ב-DB היא רשומה כ-"דוידוביץ (זיצוב) אורה" כי
--         זיצוב = שם נעורים. דוידוביץ = שם נשואה.
--         באקסל "רשימת בעלי קרקע" היא flag=0 — שגוי.
--         PDF מאשר חתימה ✅.
--
-- 📊 13 רשומות אחרות מתוך 17 — אומתו כ-לא חתומות
--    (אין להן PDF חתום באף תיקייה). נשארות not_verified.
--
-- 🔒 לוגיקה:
--   • זיהוי: id_number = '000156151' (אורה דוידוביץ)
--   • UPDATE: master_2015_status → 'verified'
--   • הערה: מתעדכנת לציין את הראיה (PDF חתום)
-- ============================================================


BEGIN;


-- ============================================================
-- שאילתה 1: לפני - כמה רשומות יתעדכנו
-- ============================================================
SELECT '📸 לפני 100' AS "שלב",
  owner_name AS "שם",
  id_number  AS "ת״ז",
  parcel     AS "חלקה",
  master_2015_status AS "סטטוס נוכחי"
FROM public.signed_owners
WHERE id_number = '000156151'
  AND is_active = TRUE
ORDER BY parcel;


-- ============================================================
-- צעד A: UPDATE — אורה דוידוביץ → verified
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'verified',
  master_2015_notes  = 'PDF הסכם חתום קיים: "ריכוז הסכמי ניהול על בעלי הקרקע/חלקה 13/אורה דוידוביץ.pdf" (וגם בחלקות 16, 49). שם בעת חתימה: דוידוביץ. שם נעורים: זיצוב. אומת ידנית 16/05/2026.'
WHERE id_number = '000156151'
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 2: אחרי - הסטטוס החדש
-- ============================================================
SELECT '✅ אחרי 100' AS "שלב",
  owner_name AS "שם",
  id_number  AS "ת״ז",
  parcel     AS "חלקה",
  master_2015_status AS "סטטוס חדש",
  LEFT(master_2015_notes, 80) AS "הערה (80 תווים)"
FROM public.signed_owners
WHERE id_number = '000156151'
  AND is_active = TRUE
ORDER BY parcel;


-- ============================================================
-- שאילתה 3: סיכום פיזור סטטוסים בסוף שלב B
-- ============================================================
SELECT '📊 סיכום סופי' AS "שלב",
  COALESCE(master_2015_status, '(NULL)') AS "סטטוס",
  COUNT(*) AS "כמות"
FROM public.signed_owners
WHERE is_active = TRUE
GROUP BY master_2015_status
ORDER BY COUNT(*) DESC;


COMMIT;


