-- 🎯 מטרה: תיקון של 3 רשומות מ-not_verified חזרה ל-verified,
--          על סמך הצלבת מקורות אמת + אישור המשתמש.
--
-- 📋 פירוט (3 בעלים, 6 רשומות סה"כ):
--
--   1. דוידוביץ (זיצוב) אורה (000156151) — 4 רשומות
--      ראיה: PDF "אורה דוידוביץ.pdf" קיים בחלקות 13, 16, 31, 49.
--      ב-DB היא רשומה כ"דוידוביץ (זיצוב) אורה" — זיצוב = שם נעורים.
--
--   2. לוין בוריס (013296694) — 1 רשומה
--      ראיה: אישור ידני של המשתמש (16/05/2026).
--      הסכם לא מתויק בתיקיות המקומיות אך המשתמש מאשר שחתם.
--
--   3. ליפ דביר (021775598) — 1 רשומה
--      ראיה: אישור ידני של המשתמש (16/05/2026).
--      המשתמש: "ליפ חתמו" (משפחת ליפ).
--
-- 📋 13 הרשומות הנותרות מתוך 17 — נשארות not_verified:
--      • גלזר מרים — לא חתמה (אומת ע"י המשתמש)
--      • דניאלי סמדר — לא חתמה (אומת ע"י המשתמש)
--      • דרורי אליהו, דרורי מיכל, סטולר אלה,
--        פלדמן אהרון (2 ת"ז), שוורצברג אריאלה — בהמתנה לבדיקה
-- ============================================================


BEGIN;


-- ============================================================
-- שאילתה 1: לפני - הרשומות שיתעדכנו
-- ============================================================
SELECT '📸 לפני 100' AS "שלב",
  owner_name AS "שם",
  id_number  AS "ת״ז",
  parcel     AS "חלקה",
  master_2015_status AS "סטטוס נוכחי"
FROM public.signed_owners
WHERE id_number IN ('000156151', '013296694', '021775598')
  AND is_active = TRUE
ORDER BY id_number, parcel;


-- ============================================================
-- צעד A: דוידוביץ (זיצוב) אורה → verified
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'verified',
  master_2015_notes  = 'PDF הסכם חתום קיים: "ריכוז הסכמי ניהול על בעלי הקרקע/חלקה 13,16,31,49/אורה דוידוביץ.pdf". שם בעת חתימה: דוידוביץ. שם נעורים: זיצוב. אומת 16/05/2026.'
WHERE id_number = '000156151'
  AND is_active = TRUE;


-- ============================================================
-- צעד B: לוין בוריס → verified (לפי אישור המשתמש)
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'verified',
  master_2015_notes  = 'אישור ידני של המשתמש 16/05/2026: בוריס לוין חתם, גם אם PDF לא תויק בתיקיות המקומיות. האקסל "רשימת בעלי קרקע" סימן בטעות flag=0.'
WHERE id_number = '013296694'
  AND is_active = TRUE;


-- ============================================================
-- צעד C: ליפ דביר → verified (לפי אישור המשתמש)
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'verified',
  master_2015_notes  = 'אישור ידני של המשתמש 16/05/2026: "ליפ חתמו" (משפחת ליפ). PDF לא תויק בתיקיות המקומיות. האקסל סימן בטעות flag=0.'
WHERE id_number = '021775598'
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 2: אחרי - הסטטוס החדש
-- ============================================================
SELECT '✅ אחרי 100' AS "שלב",
  owner_name AS "שם",
  id_number  AS "ת״ז",
  parcel     AS "חלקה",
  master_2015_status AS "סטטוס חדש",
  LEFT(master_2015_notes, 80) AS "הערה (80 תווים)"
FROM public.signed_owners
WHERE id_number IN ('000156151', '013296694', '021775598')
  AND is_active = TRUE
ORDER BY id_number, parcel;


-- ============================================================
-- שאילתה 3: סיכום פיזור סטטוסים בסוף 100
-- ============================================================
SELECT '📊 סיכום' AS "שלב",
  COALESCE(master_2015_status, '(NULL)') AS "סטטוס",
  COUNT(*) AS "כמות"
FROM public.signed_owners
WHERE is_active = TRUE
GROUP BY master_2015_status
ORDER BY COUNT(*) DESC;


COMMIT;


