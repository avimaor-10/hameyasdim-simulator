-- 🎯 מטרה: עדכון master_2015_status לפי העיקרון השמרני של המשתמש:
--          "לא מוחקים מהדאטה. בספק → needs_manual_check.
--           רק אישור ודאי של המשתמש → not_verified."
--
-- 📋 שינויים:
--
--   verified (יש ראיה חד-משמעית):
--   1. דוידוביץ (זיצוב) אורה (000156151) — 4 רשומות
--      • PDF "אורה דוידוביץ.pdf" קיים בחלקות 13, 16, 31, 49
--      • מופיעה ב"רשימת בעלי קרקע חתומים על הסכם ניהול.xlsx"
--   2. גלזר מרים (000517711) — 1 רשומה
--      • מופיעה ב"רשימת בעלי קרקע חתומים" שורה 72
--        עם 2,785.2 מ"ר בהסכם
--      • פעולה: מכר ללא תמורה (קיבלה בשרשור ממי שחתם)
--      • מחויבת להסכם דרך שרשור
--
--   needs_manual_check (יש ספק — בדיקה פיסית בקלסרים):
--   3. לוין בוריס (013296694) — יש תיקייה + יפוי כח חתום
--   4. ליפ דביר (021775598) — יש תיקייה (רק docx)
--   5. דרורי אליהו (004763094) — יש תיקייה (רק docx)
--   6. דרורי מיכל (000433318) — יש תיקייה (רק docx)
--   7. סטולר אלה (016917494) — יש תיקייה (אלה סטולר.docx)
--   8. פלדמן אהרון (001561380, 000156138) — 2 רשומות,
--      יש תיקייה עם תעודת פטירה של יעקב פלדמן בלבד
--   9. דניאלי סמדר (005141791) — 2 רשומות, אין תיקייה
--   10. שוורצברג אריאלה (005284319) — אין תיקייה
--
-- 🔒 אסור למחוק! זוהי גישה שמרנית — סטטוס שאומר
--    "צריך בדיקה ידנית" עדיף על "לא חתם".
-- ============================================================


BEGIN;


-- ============================================================
-- שאילתה 1: לפני - הרשומות שיתעדכנו
-- ============================================================
SELECT '📸 לפני 100' AS "שלב",
  owner_name AS "שם",
  id_number  AS "ת״ז",
  parcel     AS "חלקה",
  master_2015_status AS "סטטוס נוכחי"
FROM public.signed_owners
WHERE id_number IN (
  '000156151',  -- אורה דוידוביץ
  '000517711',  -- גלזר מרים
  '013296694',  -- לוין בוריס
  '021775598',  -- ליפ דביר
  '004763094',  -- דרורי אליהו
  '000433318',  -- דרורי מיכל
  '016917494',  -- סטולר אלה
  '001561380','000156138',  -- פלדמן אהרון
  '005141791',  -- דניאלי סמדר
  '005284319'   -- שוורצברג אריאלה
)
AND is_active = TRUE
ORDER BY id_number, parcel;


-- ============================================================
-- צעד A: אורה דוידוביץ → verified (PDF + רשימת מחויבים)
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'verified',
  master_2015_notes  = 'PDF הסכם חתום קיים: "ריכוז הסכמי ניהול/אורה דוידוביץ.pdf" בחלקות 13, 16, 31, 49. גם ברשימת מחויבים. שם נעורים: זיצוב. אומת 16/05/2026.'
WHERE id_number = '000156151' AND is_active = TRUE;


-- ============================================================
-- צעד B: גלזר מרים → verified (ברשימת מחויבים, מכר ללא תמורה)
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'verified',
  master_2015_notes  = 'ברשימת מחויבים להסכם הניהול (שורה 72): 2785.2 מ"ר בהסכם, פעולה: "מכר ללא תמורה" (שרשור — קיבלה ממי שחתם). הערה במקור: יש מפעל מצבות לבדיקה. אומת 16/05/2026.'
WHERE id_number = '000517711' AND is_active = TRUE;


-- ============================================================
-- צעד C: 8 בעלים → needs_manual_check (יש ספק — בדיקה ידנית)
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'needs_manual_check',
  master_2015_notes  = COALESCE(master_2015_notes || ' | ', '') ||
    'לבדיקה פיסית בקלסרים. ' ||
    CASE id_number
      WHEN '013296694' THEN 'לוין בוריס — יש תיקייה במאסטר עם יפוי כח חתום, אבל לא ברשימת מחויבים. המשתמש מסר ידנית: חתם.'
      WHEN '021775598' THEN 'ליפ דביר — יש תיקייה במאסטר (רק docx), לא ברשימת מחויבים. המשתמש מסר ידנית: חתם.'
      WHEN '004763094' THEN 'דרורי אליהו — יש תיקייה במאסטר (רק docx, אין PDF חתום). לא ברשימת מחויבים.'
      WHEN '000433318' THEN 'דרורי מיכל — יש תיקייה במאסטר (רק docx, אין PDF חתום). לא ברשימת מחויבים.'
      WHEN '016917494' THEN 'סטולר אלה (אורה?) — יש תיקייה במאסטר (רק docx). לא ברשימת מחויבים.'
      WHEN '001561380' THEN 'פלדמן אהרון — יש תיקייה עם תעודת פטירה של פלדמן יעקב. לא ברשימת מחויבים.'
      WHEN '000156138' THEN 'פלדמן אהרון (ת"ז שניה) — יש תיקייה עם תעודת פטירה של פלדמן יעקב. לא ברשימת מחויבים.'
      WHEN '005141791' THEN 'דניאלי סמדר — אין תיקייה במאסטר, לא ברשימת מחויבים. המשתמש מסר: כנראה לא חתמה.'
      WHEN '005284319' THEN 'שוורצברג אריאלה — אין תיקייה במאסטר, לא ברשימת מחויבים.'
      ELSE 'בדיקה נדרשת'
    END ||
    ' (16/05/2026)'
WHERE id_number IN (
  '013296694','021775598','004763094','000433318','016917494',
  '001561380','000156138','005141791','005284319'
)
AND is_active = TRUE;


-- ============================================================
-- שאילתה 2: אחרי - הסטטוס החדש
-- ============================================================
SELECT '✅ אחרי 100' AS "שלב",
  owner_name AS "שם",
  id_number  AS "ת״ז",
  parcel     AS "חלקה",
  master_2015_status AS "סטטוס חדש",
  LEFT(master_2015_notes, 100) AS "הערה (100 תווים)"
FROM public.signed_owners
WHERE id_number IN (
  '000156151','000517711','013296694','021775598',
  '004763094','000433318','016917494',
  '001561380','000156138','005141791','005284319'
)
AND is_active = TRUE
ORDER BY id_number, parcel;


-- ============================================================
-- שאילתה 3: סיכום פיזור סטטוסים
-- ============================================================
SELECT '📊 סיכום' AS "שלב",
  COALESCE(master_2015_status, '(NULL)') AS "סטטוס",
  COUNT(*) AS "כמות"
FROM public.signed_owners
WHERE is_active = TRUE
GROUP BY master_2015_status
ORDER BY COUNT(*) DESC;


COMMIT;


