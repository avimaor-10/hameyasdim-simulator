-- 🎯 מטרה: עדכון master_2015_status לפי העיקרון השמרני של המשתמש:
--          "לא מוחקים מהדאטה. בספק → needs_manual_check.
--           רק אישור ודאי של המשתמש → not_verified."
--
-- 📋 שינויים:
--
--   verified (יש ראיה חד-משמעית):
--   1. דוידוביץ (זיצוב) אורה (000156151) — 4 רשומות
--      • PDF "אורה דוידוביץ.pdf" קיים בחלקות 13, 16, 31, 49
--      • מופיעה ב"רשימת בעלי קרקע חתומים על הסכם ניהול.xlsx"
--   2. גלזר מרים (000517711) — 1 רשומה
--      • מופיעה ב"רשימת בעלי קרקע חתומים" שורה 72
--        עם 2,785.2 מ"ר בהסכם
--      • פעולה: מכר ללא תמורה (קיבלה בשרשור ממי שחתם)
--      • מחויבת להסכם דרך שרשור
--
--   needs_manual_check (יש ספק — בדיקה פיסית בקלסרים):
--   3. לוין בוריס (013296694) — יש תיקייה + יפוי כח חתום
--   4. ליפ דביר (021775598) — יש תיקייה (רק docx)
--   5. דרורי אליהו (004763094) — יש תיקייה (רק docx)
--   6. דרורי מיכל (000433318) — יש תיקייה (רק docx)
--   7. סטולר אלה (016917494) — יש תיקייה (אלה סטולר.docx)
--   8. פלדמן אהרון (001561380, 000156138) — 2 רשומות,
--      יש תיקייה עם תעודת פטירה של יעקב פלדמן בלבד
--   9. דניאלי סמדר (005141791) — 2 רשומות, אין תיקייה
--   10. שוורצברג אריאלה (005284319) — אין תיקייה
--
-- 🔒 אסור למחוק! זוהי גישה שמרנית — סטטוס שאומר
--    "צריך בדיקה ידנית" עדיף על "לא חתם".
-- ============================================================


BEGIN;


-- ============================================================
-- צעד מקדים: הרחבת CHECK constraint לכלול 'needs_manual_check'
-- ============================================================
-- ה-constraint הישן (מסקריפט 09) מאפשר רק 'verified'/'not_verified'/'not_checked'.
-- אנחנו רוצים להוסיף ערך 'needs_manual_check' כדי לסמן רשומות
-- שדורשות בדיקה פיסית בקלסרים.
ALTER TABLE public.signed_owners
  DROP CONSTRAINT IF EXISTS signed_owners_master_2015_status_check;

ALTER TABLE public.signed_owners
  ADD CONSTRAINT signed_owners_master_2015_status_check
  CHECK (master_2015_status IN ('verified', 'not_verified', 'not_checked', 'needs_manual_check'));


-- ============================================================
-- שאילתה 1: לפני - הרשומות שיתעדכנו
-- ============================================================
SELECT '📸 לפני 100' AS "שלב",
  owner_name AS "שם",
  id_number  AS "ת״ז",
  parcel     AS "חלקה",
  master_2015_status AS "סטטוס נוכחי"
FROM public.signed_owners
WHERE id_number IN (
  '000156151',  -- אורה דוידוביץ
  '000517711',  -- גלזר מרים
  '013296694',  -- לוין בוריס
  '021775598',  -- ליפ דביר
  '004763094',  -- דרורי אליהו
  '000433318',  -- דרורי מיכל
  '016917494',  -- סטולר אלה
  '001561380','000156138',  -- פלדמן אהרון
  '005141791',  -- דניאלי סמדר
  '005284319'   -- שוורצברג אריאלה
)
AND is_active = TRUE
ORDER BY id_number, parcel;


-- ============================================================
-- צעד A: אורה דוידוביץ → verified (PDF + רשימת מחויבים)
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'verified',
  master_2015_notes  = 'PDF הסכם חתום קיים: "ריכוז הסכמי ניהול/אורה דוידוביץ.pdf" בחלקות 13, 16, 31, 49. גם ברשימת מחויבים. שם נעורים: זיצוב. אומת 16/05/2026.'
WHERE id_number = '000156151' AND is_active = TRUE;


-- ============================================================
-- צעד B: גלזר מרים → verified (ברשימת מחויבים, מכר ללא תמורה)
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'verified',
  master_2015_notes  = 'ברשימת מחויבים להסכם הניהול (שורה 72): 2785.2 מ"ר בהסכם, פעולה: "מכר ללא תמורה" (שרשור — קיבלה ממי שחתם). הערה במקור: יש מפעל מצבות לבדיקה. אומת 16/05/2026.'
WHERE id_number = '000517711' AND is_active = TRUE;


-- ============================================================
-- צעד B2: פלדמן אהרון → verified (שרשור משפחה שנייה של פלדמן)
-- ============================================================
-- 🔍 הקשר: אהרון ירש בחלקות 13, 49 (ירושה לפי טאבו).
-- אחיו (יוסף, דוד, אמנון, ברקוביץ-פלדמן רחל) - כולם חתמו ומסומנים verified ב-DB.
-- האב של המשפחה השנייה (לא יעקב פלדמן! יעקב הוא משפחה אחרת)
-- חתם על ההסכם, וכל ילדיו ירשו. אהרון בשרשור החתום.
UPDATE public.signed_owners
SET
  master_2015_status = 'verified',
  master_2015_notes  = 'יורש במשפחת פלדמן השנייה (אחיו של יוסף, דוד, אמנון, ברקוביץ-פלדמן רחל - כולם חתמו). בטאבו: ירושה בחלקות 13, 49 (1/8 ו-1/16). שרשור משפחתי תקין. אומת 16/05/2026 לאחר הצלבת טאבו ואישור משתמש.'
WHERE id_number IN ('001561380', '000156138')
  AND is_active = TRUE;


-- ============================================================
-- צעד B3: לוין בוריס → verified (אישור משתמש: חתם, סביר ב-2021)
-- ============================================================
-- 🔍 הקשר: בטאבו חלקה 7 — מכר 1/6 (849.33 מ"ר) מ-19/02/1997.
-- המשתמש אישר ב-16/05/2026 שחתם, ייתכן בשלב מאוחר (סביבות 2021).
-- בקלסר הפיסי: יפוי כח חתום (`864639יפוי כח חתום - לוין בוריס.pdf`).
UPDATE public.signed_owners
SET
  master_2015_status = 'verified',
  master_2015_notes  = 'בטאבו חלקה 7: מכר 1/6 (849.33 מ"ר) מ-19/02/1997. חתם על הסכם הניהול (ייתכן בשלב מאוחר, סביבות 2021 — אחרי שנערכה רשימת 2015). בקלסר: יפוי כח חתום. אומת ע"י המשתמש 16/05/2026.'
WHERE id_number = '013296694' AND is_active = TRUE;


-- ============================================================
-- צעד B4: ליפ דביר → verified (משפחת ליפ חתמה, האב העביר ב-2019)
-- ============================================================
-- 🔍 הקשר: ליפ דביר ואייל הם אחים. האב (אלי/אליהו ליפ) העביר להם
-- את הקרקע ב-06/10/2019 ("מכר ללא תמורה" 1/12 כל אחד).
-- האב חתם, כך הבנים בשרשור החתום.
-- המשתמש אישר 16/05/2026: משפחת ליפ חתמה (ייתכן בשלב מאוחר ~2021).
UPDATE public.signed_owners
SET
  master_2015_status = 'verified',
  master_2015_notes  = 'משפחת ליפ — דביר ואייל אחים. האב אלי/אליהו ליפ העביר לבנים ב-06/10/2019 (מכר ללא תמורה, 1/12 כל אחד בחלקה 7). שרשור משפחתי תקין — האב חתם. המשתמש אישר חתימה (ייתכן בשלב מאוחר ~2021). אומת 16/05/2026.'
WHERE id_number = '021775598' AND is_active = TRUE;


-- ============================================================
-- צעד C: 8 בעלים → needs_manual_check (יש ספק — בדיקה ידנית)
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'needs_manual_check',
  master_2015_notes  = COALESCE(master_2015_notes || ' | ', '') ||
    'לבדיקה פיסית בקלסרים. ' ||
    CASE id_number
      WHEN '013296694' THEN 'לוין בוריס — חלקה 7. בטאבו: מכר 1/6 (849.33 מ"ר). יש תיקייה+יפוי כח חתום (אין הסכם ניהול). לא ברשימת מחויבים. המשתמש מסר: חתם.'
      WHEN '021775598' THEN 'ליפ דביר — חלקה 7. בטאבו: מכר ללא תמורה 1/12 (424.67 מ"ר). יש תיקייה (רק docx). לא ברשימת מחויבים. המשתמש מסר: חתם.'
      WHEN '004763094' THEN 'דרורי אליהו — חלקה 17. בטאבו: מכר 500/16987 (500 מ"ר). יש תיקייה ("מכתב לרוכשים"). אין PDF חתום. לא ברשימת מחויבים.'
      WHEN '000433318' THEN 'דרורי מיכל — חלקה 17. בטאבו: מכר 500/16987 (500 מ"ר). אותם מסמכים כמו דרורי אליהו (זוג).'
      WHEN '016917494' THEN 'סטולר אלה — חלקה 8. בטאבו: צירוף חלקים (500 מ"ר). תיקייה בשם "סטולר אורה" אבל מסמכים "אלה סטולר". אין PDF חתום.'
      WHEN '001561380' THEN 'פלדמן אהרון — חלקות 13, 49. בטאבו: ירושה. יש תיקייה עם תעודת פטירה של פלדמן יעקב. בני משפחה ירשו: אמנון, דוד, יוסף, ברקוביץ-פלדמן רחל, שרוני ענבר. סביר: יורש של קרוב משפחה אחר של יעקב.'
      WHEN '000156138' THEN 'פלדמן אהרון (ת"ז שניה — אולי כפילות) — חלקות 13, 49. בטאבו: ירושה. ייתכן תיקון טעות סופר של 001561380.'
      WHEN '005141791' THEN 'דניאלי סמדר — חלקות 13, 49. בטאבו: צוואה 1/96 ו-1/192 (~8.91 מ"ר). אין תיקייה במאסטר. צריך לבדוק מי המוריש.'
      WHEN '005284319' THEN 'שוורצברג אריאלה — חלקות 13, 49. בטאבו: צוואה 1/96 ו-1/192 (~8.91 מ"ר). אין תיקייה במאסטר. צריך לבדוק מי המוריש.'
      ELSE 'בדיקה נדרשת'
    END ||
    ' (16/05/2026)'
WHERE id_number IN (
  -- '013296694', '021775598'  ← הועברו ל-verified (לוין בוריס, ליפ דביר)
  -- '001561380', '000156138'  ← הועברו ל-verified (פלדמן אהרון)
  '004763094','000433318','016917494',
  '005141791','005284319'
)
AND is_active = TRUE;


-- ============================================================
-- שאילתה 2: אחרי - הסטטוס החדש
-- ============================================================
SELECT '✅ אחרי 100' AS "שלב",
  owner_name AS "שם",
  id_number  AS "ת״ז",
  parcel     AS "חלקה",
  master_2015_status AS "סטטוס חדש",
  LEFT(master_2015_notes, 100) AS "הערה (100 תווים)"
FROM public.signed_owners
WHERE id_number IN (
  '000156151','000517711','013296694','021775598',
  '004763094','000433318','016917494',
  '001561380','000156138','005141791','005284319'
)
AND is_active = TRUE
ORDER BY id_number, parcel;


-- ============================================================
-- שאילתה 3: סיכום פיזור סטטוסים
-- ============================================================
SELECT '📊 סיכום' AS "שלב",
  COALESCE(master_2015_status, '(NULL)') AS "סטטוס",
  COUNT(*) AS "כמות"
FROM public.signed_owners
WHERE is_active = TRUE
GROUP BY master_2015_status
ORDER BY COUNT(*) DESC;


COMMIT;


