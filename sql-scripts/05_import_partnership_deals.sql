  category,
  COUNT(*)               AS deal_count,
  SUM(area_sqm)          AS total_area_sqm,
  SUM(area_sqm) FILTER (WHERE status = 'הושלמה ונרשמה')   AS registered_sqm,
  SUM(area_sqm) FILTER (WHERE status = 'הושלמה ולא נרשמה') AS unregistered_sqm,
  SUM(area_sqm) FILTER (WHERE status = 'בתהליך')          AS in_progress_sqm
FROM public.partnership_deals
WHERE is_active = TRUE
GROUP BY category
ORDER BY category;

