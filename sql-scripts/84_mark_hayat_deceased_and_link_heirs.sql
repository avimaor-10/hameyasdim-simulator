-- 🎯 מטרה: לסמן את חייט רות ז"ל (000156152) כ-is_active=FALSE
--          ולקשר את 3 יורשיה (לפי נסח טאבו 53, שטר 27004/2016/5).
--
-- 📜 מקור הנתון: נסח טאבו מאי 2026 של חלקה 53 — "מכר ללא תמורה"
--    בתאריך 16/06/2016 ל-3 בני משפחה שווים (כל אחד 1/15):
--      • דרומי אסתר (050501121)
--      • שקד הדר (051496685)
--      • בן דוד מרב (055529945)
--
-- 🧮 אימות מתמטי:
--    חלקה 53: 3 × 861.73 = 2,585.19 = 3 × (12,926 × 1/15)
--    שטח חייט רות ב-DB: 861.73 = 12,926 × 1/15 ✅
--
-- 🔒 BEGIN ... COMMIT אוטומטי (אישור מראש).
-- ============================================================


BEGIN;


-- ============================================================
-- שאילתה 1: לפני
-- ============================================================
SELECT
  '📸 לפני' AS "שלב",
  s.owner_name AS "שם",
  s.id_number AS "ת.ז.",
  s.parcel AS "חלקה",
  s.agreement_area AS "שטח",
  s.is_active AS "פעיל?",
  COALESCE(s.master_2015_status, '(NULL)') AS "סטטוס",
  COALESCE(s.inherited_from_id_number, '(NULL)') AS "ירש מ-"
FROM public.signed_owners s
WHERE s.id_number IN ('000156152', '050501121', '051496685', '055529945')
  AND (s.parcel = 53 OR s.id_number = '000156152')
ORDER BY s.is_active DESC, s.id_number, s.parcel;


-- ============================================================
-- שאילתה 2: סימון חייט רות ז"ל כלא פעילה
-- ============================================================
UPDATE public.signed_owners
SET
  is_active = FALSE,
  master_2015_notes = 'נפטרה לפני 16/06/2016. יורשים פעילים בקבוצה (לפי שטר "מכר ללא תמורה" 27004/2016/5, נסח טאבו 53): דרומי אסתר (050501121), שקד הדר (051496685), בן דוד מרב (055529945) — כל אחד 1/15 מהחלקה (861.73 מ"ר). סה"כ ירושה: ~2,585 מ"ר.'
WHERE id_number = '000156152'  -- חייט רות ז"ל
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 3: עדכון 3 יורשים — סטטוס + קישור (רק לחלקה 53!)
-- ⚠ הערה חשובה: דרומי אסתר/שקד הדר/בן דוד מרב מופיעים גם בחלקות 9/18/21.
--   שם הם בעלי קרקע עצמאיים (לא יורשי חייט רות).
--   העדכון של inherited_from_id_number הוא רק לרשומה של חלקה 53.
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'successor_to_2015_signer',
  inherited_from_id_number = '000156152',  -- חייט רות ז"ל
  master_2015_notes = COALESCE(master_2015_notes, '') ||
    CASE
      WHEN COALESCE(master_2015_notes, '') = '' THEN ''
      ELSE ' | '
    END ||
    'ירש/ה מ-חייט רות ז"ל (ת.ז. 000156152). חלקו 1/3 מתוך שטחה בחלקה 53 (לפי שטר 27004/2016/5, "מכר ללא תמורה" 16/06/2016).'
WHERE id_number IN ('050501121', '051496685', '055529945')
  AND parcel = 53  -- ⚠ רק בחלקה 53 (החלקה שירשו בה מחייט רות)
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 4: אחרי
-- ============================================================
SELECT
  '✅ אחרי' AS "שלב",
  s.owner_name AS "שם",
  s.id_number AS "ת.ז.",
  s.parcel AS "חלקה",
  s.agreement_area AS "שטח",
  s.is_active AS "פעיל?",
  s.master_2015_status AS "סטטוס",
  s.inherited_from_id_number AS "ירש מ-"
FROM public.signed_owners s
WHERE s.id_number IN ('000156152', '050501121', '051496685', '055529945')
  AND (s.parcel = 53 OR s.id_number = '000156152')
ORDER BY s.is_active DESC, s.id_number, s.parcel;


COMMIT;


