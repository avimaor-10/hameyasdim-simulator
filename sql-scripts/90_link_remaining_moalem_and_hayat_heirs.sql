-- 🎯 מטרה: לסיים את קישור היורשים שסקריפט 88 פספס.
--
-- 📜 רקע: סקריפט 88 קישר רק יורשים שב-master_2015_notes שלהם
--    כתוב "אליהו מועלם". יורשים עם הערה ריקה / NULL לא נתפסו.
--    בחלקה 36 (וכנראה גם 9, 18, 34) — 6 ילדי מועלם אליהו ז"ל
--    נשארו עם inherited_from_id_number=NULL.
--
--    בנוסף — 3 יורשי חייט רות ז"ל (דרומי, שקד, בן דוד) מקושרים
--    רק בחלקה 53 (סקריפט 84), אבל הם גם בחלקה 36 בנסח (1/15 כ"א).
--
-- 📐 פעולות:
--   A. קישור 8 ילדי מועלם אליהו ז"ל לפי ת.ז. ספציפיות (לכל הרשומות)
--      → inherited_from_id_number = '000755793'
--      → master_2015_status = 'successor_to_2015_signer'
--   B. קישור 3 יורשי חייט רות ז"ל בכל החלקות (לפי ת.ז.)
--      → inherited_from_id_number = '000156152'
--      → master_2015_status = 'successor_to_2015_signer'
--
-- ⚠ אזהרה: לא דורסים אם יש כבר inherited_from_id_number — רק ממלאים NULL.
-- ============================================================


BEGIN;


-- ============================================================
-- שאילתה 1: לפני — כמה לא-מקושרים?
-- ============================================================
SELECT '📸 לפני' AS "שלב",
  COUNT(*) FILTER (
    WHERE id_number IN ('024008567','024870347','025176629','028706828',
                        '036145704','038608923','043154855','066425737')
      AND inherited_from_id_number IS NULL
      AND is_active = TRUE
  ) AS "ילדי מועלם ללא קישור",
  COUNT(*) FILTER (
    WHERE id_number IN ('050501121','051496685','055529945')
      AND inherited_from_id_number IS NULL
      AND is_active = TRUE
  ) AS "יורשי חייט ללא קישור"
FROM public.signed_owners;


-- ============================================================
-- צעד A: קישור 8 ילדי מועלם אליהו ז"ל (000755793)
-- ============================================================
UPDATE public.signed_owners
SET
  inherited_from_id_number = '000755793',
  master_2015_status = 'successor_to_2015_signer',
  master_2015_notes = COALESCE(master_2015_notes, '') ||
    CASE WHEN COALESCE(master_2015_notes, '') = '' THEN '' ELSE ' | ' END ||
    'קושר 15/05/2026 (סקריפט 90): יורש/ת מ-מועלם אליהו ז"ל (ת.ז. 000755793) לפי שטר ירושה 406/2026/1 מיום 04/01/2026.'
WHERE id_number IN (
    '024008567',  -- מור חנן
    '024870347',  -- מור יעקב
    '025176629',  -- סופר מור לימור
    '028706828',  -- מועלם מור שרון
    '036145704',  -- מור נתנאל
    '038608923',  -- מור פנחס
    '043154855',  -- מור גלעד
    '066425737'   -- מנדל רויטל
  )
  AND inherited_from_id_number IS NULL
  AND is_active = TRUE;


-- ============================================================
-- צעד B: קישור 3 יורשי חייט רות ז"ל (000156152)
-- ============================================================
UPDATE public.signed_owners
SET
  inherited_from_id_number = '000156152',
  master_2015_status = 'successor_to_2015_signer',
  master_2015_notes = COALESCE(master_2015_notes, '') ||
    CASE WHEN COALESCE(master_2015_notes, '') = '' THEN '' ELSE ' | ' END ||
    'קושר 15/05/2026 (סקריפט 90): יורש/ת מ-חייט רות ז"ל (ת.ז. 000156152) לפי שטר 27004/2016/4 מיום 16/06/2016.'
WHERE id_number IN (
    '050501121',  -- דרומי אסתר
    '051496685',  -- שקד הדר
    '055529945'   -- בן דוד מרב
  )
  AND inherited_from_id_number IS NULL
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 2: אחרי — כמה עוד נשארו?
-- ============================================================
SELECT '✅ אחרי' AS "שלב",
  COUNT(*) FILTER (
    WHERE id_number IN ('024008567','024870347','025176629','028706828',
                        '036145704','038608923','043154855','066425737')
      AND inherited_from_id_number = '000755793'
      AND is_active = TRUE
  ) AS "ילדי מועלם מקושרים עכשיו",
  COUNT(*) FILTER (
    WHERE id_number IN ('050501121','051496685','055529945')
      AND inherited_from_id_number = '000156152'
      AND is_active = TRUE
  ) AS "יורשי חייט מקושרים עכשיו",
  COUNT(*) FILTER (
    WHERE id_number IN ('024008567','024870347','025176629','028706828',
                        '036145704','038608923','043154855','066425737',
                        '050501121','051496685','055529945')
      AND inherited_from_id_number IS NULL
      AND is_active = TRUE
  ) AS "עדיין NULL (אמור להיות 0)"
FROM public.signed_owners;


-- ============================================================
-- שאילתה 3: סיכום פילוח — כמה ב-DB עם inherited_from
-- ============================================================
SELECT
  COUNT(DISTINCT id_number) FILTER (WHERE inherited_from_id_number IS NOT NULL) AS "יורשים ייחודיים מקושרים",
  COUNT(*) FILTER (WHERE inherited_from_id_number IS NOT NULL AND is_active = TRUE) AS "רשומות מקושרות פעילות",
  COUNT(*) FILTER (
    WHERE master_2015_status = 'successor_to_2015_signer'
      AND inherited_from_id_number IS NULL
      AND is_active = TRUE
  ) AS "עדיין successor ללא קישור"
FROM public.signed_owners;


COMMIT;


