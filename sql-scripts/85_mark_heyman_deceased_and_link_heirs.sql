-- 🎯 מטרה: לסמן את תמר היימן ז"ל (000156320) כ-is_active=FALSE
--          ולקשר את 3 יורשיה (לפי נסחי טאבו 13 + 31).
--
-- 📜 מקור הנתון: נסחי טאבו מאי 2026
--    • שטר 17268/2022/4 (16/03/2022): הימן רן ישראל + הימן דוד
--    • שטר 19589/2026/1 (29/04/2026): היימן קלרה
--    כל אחד 1/48 בחלקה 31 (=שליש מ-1/16 = שליש מתמר היימן).
--
-- 🧮 אימות מתמטי:
--    חלקה 31: 3 × 463.96 = 1,391.88 ≈ 1,391.88 (שטח תמר ב-DB) ✅
--    חלקה 13: 3 × 3.15 = 9.45 ≈ 9.44 (שטח תמר ב-DB) ✅
--
-- ⚠ הערה: זוהה ב-DB רק "הימן דוד" (051194918) בצילומי המסך.
--    סקריפט זה יעדכן גם את 2 האחרים אם הם קיימים — אם לא,
--    ה-UPDATE פשוט לא ישפיע עליהם.
--
-- 🔒 BEGIN ... COMMIT אוטומטי (אישור מראש).
-- ============================================================


BEGIN;


-- ============================================================
-- שאילתה 1: לפני
-- ============================================================
SELECT
  '📸 לפני' AS "שלב",
  s.owner_name AS "שם",
  s.id_number AS "ת.ז.",
  s.parcel AS "חלקה",
  s.agreement_area AS "שטח",
  s.is_active AS "פעיל?",
  COALESCE(s.master_2015_status, '(NULL)') AS "סטטוס",
  COALESCE(s.inherited_from_id_number, '(NULL)') AS "ירש מ-"
FROM public.signed_owners s
WHERE s.id_number IN ('000156320', '003631074', '051194918', '014777221')
ORDER BY s.is_active DESC, s.id_number, s.parcel;


-- ============================================================
-- שאילתה 2: סימון תמר היימן ז"ל כלא פעילה
-- ============================================================
UPDATE public.signed_owners
SET
  is_active = FALSE,
  master_2015_notes = 'נפטרה לפני 16/03/2022. יורשים פעילים בקבוצה (לפי נסחי טאבו 13+31): הימן רן ישראל (003631074), הימן דוד (051194918), היימן קלרה (014777221) — כל אחד 1/48 בחלקה. שטרי ירושה: 17268/2022/4 + 19589/2026/1.'
WHERE id_number = '000156320'  -- תמר היימן ז"ל
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 3: עדכון 3 יורשים — סטטוס + קישור (בחלקות 13 + 31)
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'successor_to_2015_signer',
  inherited_from_id_number = '000156320',  -- תמר היימן ז"ל
  master_2015_notes = COALESCE(master_2015_notes, '') ||
    CASE
      WHEN COALESCE(master_2015_notes, '') = '' THEN ''
      ELSE ' | '
    END ||
    'ירש/ה מ-תמר היימן ז"ל (ת.ז. 000156320). חלקו 1/3 מתוך שטחה (לפי נסחי טאבו 13+31).'
WHERE id_number IN ('003631074', '051194918', '014777221')
  AND parcel IN (13, 31)
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 4: אחרי
-- ============================================================
SELECT
  '✅ אחרי' AS "שלב",
  s.owner_name AS "שם",
  s.id_number AS "ת.ז.",
  s.parcel AS "חלקה",
  s.agreement_area AS "שטח",
  s.is_active AS "פעיל?",
  s.master_2015_status AS "סטטוס",
  s.inherited_from_id_number AS "ירש מ-"
FROM public.signed_owners s
WHERE s.id_number IN ('000156320', '003631074', '051194918', '014777221')
ORDER BY s.is_active DESC, s.id_number, s.parcel;


COMMIT;


