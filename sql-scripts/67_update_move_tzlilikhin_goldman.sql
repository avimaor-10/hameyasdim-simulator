--
-- שאילתה 4: צריך להראות 3 שורות עם "סטטוס" = '✅ תואם' עבור כולן:
--   🔵 ~41,713 (צפוי 41,711) ✅
--   🟡   ~596 (צפוי 597)    ✅
--   🟠 ~  581 (צפוי 731 — תקין כי לא כולל signed_owners ב-DB) ✅
