-- 🎯 מטרה: להעביר 2 עסקאות מקבוצת "4 חברות פרטיות" לקבוצת
--          "רוכשי שותפות נדל"ן נדל"ן קבוצת מור".
--
-- 📋 רקע (לפי האקסל המתוקן + סקריפט 66 אימת):
--   • עסקת צלליכין (חלקה 17, ~2,832 מ"ר) — עסקה מלאה ששייכת ל-🔵 השותפות
--   • עסקת גולדמן  (חלקה 17, ~2,832 מ"ר) — עסקה מלאה ששייכת ל-🔵 השותפות
--
-- 🔢 התוצאה הצפויה אחרי ההעברה:
--   🔵 רוכשי שותפות נדלן נדלן קבוצת מור: 36,049 → 41,711 (+5,664)
--   🟡 קבוצת 4 חברות פרטיות:              6,260 → 597     (-5,664)
--   🟠 קבוצת חנן מור השקעות:                731 → 731     (ללא שינוי)
--
-- ⚙ מבנה הסקריפט:
--   BEGIN; (טרנזקציה — לא נשמר ב-DB עד COMMIT)
--   ↓
--   [שאילתה 1] לצלם מצב לפני
--   [שאילתה 2] UPDATE (משנה בתוך טרנזקציה זמנית)
--   [שאילתה 3] לצלם מצב אחרי
--   [שאילתה 4] סיכום קבוצות אחרי
--   ↓
--   -- COMMIT;     ← תוריד את ה-"--" רק אחרי שאתה רואה שהמספרים נכונים!
--   -- ROLLBACK;   ← תוריד את ה-"--" אם משהו נראה לא תקין
--
-- ⚠ עד שאתה לא מריץ "COMMIT;" — שום שינוי לא נשמר ב-DB.
-- ⚠ אסור לסגור את הטאב לפני שאתה COMMIT/ROLLBACK!
-- ============================================================


BEGIN;


-- ============================================================
-- [שאילתה 1] צילום מצב לפני UPDATE
-- ============================================================
SELECT
  '📸 לפני UPDATE' AS "שלב",
  pd.id::text AS "deal_id",
  pd.deal_number AS "#",
  pd.deal_name AS "שם עסקה",
  pd.parcel AS "חלקה",
  ROUND(pd.area_sqm::numeric, 2) AS "שטח",
  fg.family_name AS "קבוצה נוכחית"
FROM public.partnership_deals pd
LEFT JOIN public.family_groups fg ON fg.id = pd.family_group_id
WHERE pd.is_active = TRUE
  AND (pd.deal_name ILIKE '%צלליכין%' OR pd.deal_name ILIKE '%גולדמן%')
  AND pd.family_group_id = 'f116cce6-ddd8-47ca-acd2-a92a2daa1442'
ORDER BY pd.deal_name;


-- ============================================================
-- [שאילתה 2] UPDATE — העברה ל-🔵 רוכשי שותפות נדל"ן נדל"ן קבוצת מור
-- ============================================================
-- 🛡 הגנה כפולה:
--   1. WHERE על שם העסקה (צלליכין OR גולדמן)
--   2. WHERE על family_group_id הנוכחי (חייב להיות 🟡 4 חברות) — מונע פגיעה במקום אחר
--   3. AND is_active = TRUE — רק עסקאות פעילות
UPDATE public.partnership_deals
SET family_group_id = '50ee16ff-4025-479d-af57-9f56d0489305'  -- 🔵 רוכשי שותפות
WHERE (deal_name ILIKE '%צלליכין%' OR deal_name ILIKE '%גולדמן%')
  AND family_group_id = 'f116cce6-ddd8-47ca-acd2-a92a2daa1442'  -- 🟡 4 חברות (מצב נוכחי)
  AND is_active = TRUE;


-- ============================================================
-- [שאילתה 3] צילום מצב אחרי UPDATE (אותן עסקאות)
-- ============================================================
SELECT
  '📸 אחרי UPDATE' AS "שלב",
  pd.id::text AS "deal_id",
  pd.deal_number AS "#",
  pd.deal_name AS "שם עסקה",
  pd.parcel AS "חלקה",
  ROUND(pd.area_sqm::numeric, 2) AS "שטח",
  fg.family_name AS "קבוצה חדשה"
FROM public.partnership_deals pd
LEFT JOIN public.family_groups fg ON fg.id = pd.family_group_id
WHERE pd.is_active = TRUE
  AND (pd.deal_name ILIKE '%צלליכין%' OR pd.deal_name ILIKE '%גולדמן%')
ORDER BY pd.deal_name;


-- ============================================================
-- [שאילתה 4] סיכום סופי — סך 3 הקבוצות אחרי UPDATE
-- ============================================================
SELECT
  '🎯 סיכום קבוצות אחרי UPDATE' AS "מדד",
  fg.family_name AS "קבוצה",
  COUNT(pd.id) AS "מס׳ עסקאות",
  ROUND(SUM(pd.area_sqm)::numeric, 2) AS "סך שטח מר",
  ROUND((SUM(pd.area_sqm) / 1000)::numeric, 2) AS "דונם",
  CASE fg.id
    WHEN '50ee16ff-4025-479d-af57-9f56d0489305'::uuid THEN 41711
    WHEN 'f116cce6-ddd8-47ca-acd2-a92a2daa1442'::uuid THEN 597
    WHEN '42687af3-4ee2-48f6-b1b7-5f74c4eae699'::uuid THEN 731
  END AS "צפוי",
  CASE
    WHEN ABS(SUM(pd.area_sqm) - (
      CASE fg.id
        WHEN '50ee16ff-4025-479d-af57-9f56d0489305'::uuid THEN 41711
        WHEN 'f116cce6-ddd8-47ca-acd2-a92a2daa1442'::uuid THEN 597
        WHEN '42687af3-4ee2-48f6-b1b7-5f74c4eae699'::uuid THEN 731
      END
    )) < 50 THEN '✅ תואם'
    ELSE '⚠ פער — לא לעשות COMMIT!'
  END AS "סטטוס"
FROM public.partnership_deals pd
JOIN public.family_groups fg ON fg.id = pd.family_group_id
WHERE pd.is_active = TRUE
  AND fg.id IN (
    '50ee16ff-4025-479d-af57-9f56d0489305',
    'f116cce6-ddd8-47ca-acd2-a92a2daa1442',
    '42687af3-4ee2-48f6-b1b7-5f74c4eae699'
  )
GROUP BY fg.id, fg.family_name
ORDER BY SUM(pd.area_sqm) DESC;


