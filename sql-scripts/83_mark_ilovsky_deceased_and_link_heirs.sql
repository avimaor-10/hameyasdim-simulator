-- 🎯 מטרה: לסמן את יפה בוקטר ילובסקי ז"ל (000156538) כ-is_active=FALSE
--          ולקשר את 4 יורשיה (לפי נסחי טאבו 28 + 51, שטר 4014/2016/3).
--
-- 📜 מקור הנתון: נסחי טאבו מאי 2026 של חלקות 28 ו-51 — ירושה
--    בתאריך 26/01/2016 ל-4 אנשים שווים (כל אחד 31/2000 = 1.55%):
--      • בוקסר משה (001565399)
--      • שילוח אהודה (001565407)
--      • גרצברג ליאורה (001565415)
--      • בוקסר און (051608941)
--
-- 🧮 אימות מתמטי:
--    חלקה 28: 4 × 423.69 = 1,694.76 מ"ר ≈ 1,694.77 (שטח יפה ז"ל ב-DB) ✅
--    חלקה 51: 4 × 386.23 = 1,544.92 מ"ר ≈ 1,544.92 (שטח יפה ז"ל ב-DB) ✅
--
-- 🚨 חשוב: יש כפל ספירה. גם יפה ז"ל וגם 4 יורשיה רשומים עם אותו שטח.
--    סימון יפה ז"ל כ-FALSE יסיר את הכפילות.
--
-- 🔒 BEGIN ... COMMIT אוטומטי (אישור מראש).
-- ============================================================


BEGIN;


-- ============================================================
-- שאילתה 1: לפני — צילום מצב
-- ============================================================
SELECT
  '📸 לפני' AS "שלב",
  s.owner_name AS "שם",
  s.id_number AS "ת.ז.",
  s.parcel AS "חלקה",
  s.agreement_area AS "שטח",
  s.is_active AS "פעיל?",
  COALESCE(s.master_2015_status, '(NULL)') AS "סטטוס",
  COALESCE(s.inherited_from_id_number, '(NULL)') AS "ירש מ-"
FROM public.signed_owners s
WHERE s.id_number IN ('000156538', '001565399', '001565407', '001565415', '051608941')
ORDER BY s.is_active DESC, s.id_number, s.parcel;


-- ============================================================
-- שאילתה 2: סימון יפה בוקטר ילובסקי ז"ל כלא פעילה + הערה
-- ============================================================
UPDATE public.signed_owners
SET
  is_active = FALSE,
  master_2015_notes = 'נפטרה לפני 26/01/2016. יורשים פעילים בקבוצה (לפי שטר ירושה 4014/2016/3, נסחי טאבו 28+51): בוקסר משה (001565399), שילוח אהודה (001565407), גרצברג ליאורה (001565415), בוקסר און (051608941) — כל אחד 31/2000 מהחלקה. סה"כ ירושה: ~3,239 מ"ר.'
WHERE id_number = '000156538'  -- יפה בוקטר ילובסקי ז"ל
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 3: עדכון 4 היורשים — סטטוס + קישור הדדי
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'successor_to_2015_signer',
  inherited_from_id_number = '000156538',  -- יפה בוקטר ילובסקי ז"ל
  master_2015_notes = COALESCE(master_2015_notes, '') ||
    CASE
      WHEN COALESCE(master_2015_notes, '') = '' THEN ''
      ELSE ' | '
    END ||
    'ירש/ה מ-יפה בוקטר ילובסקי ז"ל (ת.ז. 000156538). חלקו 1/4 מתוך שטחה (לפי שטר ירושה 4014/2016/3, נסח טאבו מאי 2026).'
WHERE id_number IN ('001565399', '001565407', '001565415', '051608941')
  AND parcel IN (28, 51)
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 4: אחרי — אימות
-- ============================================================
SELECT
  '✅ אחרי' AS "שלב",
  s.owner_name AS "שם",
  s.id_number AS "ת.ז.",
  s.parcel AS "חלקה",
  s.agreement_area AS "שטח",
  s.is_active AS "פעיל?",
  s.master_2015_status AS "סטטוס",
  s.inherited_from_id_number AS "ירש מ-",
  LEFT(s.master_2015_notes, 80) AS "הערה (80 תווים)"
FROM public.signed_owners s
WHERE s.id_number IN ('000156538', '001565399', '001565407', '001565415', '051608941')
ORDER BY s.is_active DESC, s.id_number, s.parcel;


-- ============================================================
-- שאילתה 5: בדיקת JOIN — היורשים והמורישה
-- ============================================================
SELECT
  heir.owner_name AS "יורש",
  heir.id_number AS "ת.ז. יורש",
  heir.parcel AS "חלקה",
  ROUND(heir.agreement_area::numeric, 2) AS "שטח",
  signer.owner_name AS "שם המורישה",
  signer.is_active AS "מורישה פעילה?"
FROM public.signed_owners heir
LEFT JOIN public.signed_owners signer
  ON signer.id_number = heir.inherited_from_id_number
WHERE heir.inherited_from_id_number = '000156538'
  AND heir.is_active = TRUE
ORDER BY heir.id_number, heir.parcel;


COMMIT;


