-- 🎯 מטרה: לתקן את מבנה קבוצת דודי יצחקי כך שהשטח הכולל יוצג
--          כ-13,698.5 מ"ר (כעת מוצג כ-~5,037 בגלל בעיית קיזוז).
--
-- 📐 המבנה הסופי המבוקש (חלקה 14):
--   1. וזוב                       — 6,185 (כבר תקין)
--   2. רוכשי וזוב                  — 1,987 (כעת 1,487 → +500)
--   3. ולנסיה                     — 990   (כבר תקין)
--   4. מחלבות המושבה              — 995   (כעת 1,245 → -250)
--   5. דסטא.ח אחזקות              — 995   (כעת 1,245 → -250)
--   6. ראובן באבי                 — 500   (כבר תקין)
--   7. אליהו באבי                 — 500   (כבר תקין)
--   סה"כ חלקה 14: 12,152 מ"ר
--
-- 📐 חלקה 19: 21 רוכשי זייגרמן = ~1,546.5 מ"ר
-- 📊 סה"כ כל הקבוצה: 13,698.5 מ"ר
--
-- 🔧 שינויים:
--   A. הסרת עסקת יצחקי-דנקנר מהקבוצה (family_group_id = NULL)
--   B. מחיקת deal_replaced_owners של אותה עסקה
--   C. עדכון שטחים (3 רשומות)
--
-- 🆔 קבוצת יצחקי: a990ed96-5b11-4d1a-9db1-89c76833af8b
-- ============================================================


BEGIN;


-- ============================================================
-- שאילתה 1: זיהוי עסקת יצחקי-דנקנר (חלקה 14)
-- ============================================================
SELECT
  '🔍 לפני — עסקת חלקה 14 בקבוצה' AS "שלב",
  pd.id AS "deal_id",
  pd.deal_name AS "שם",
  pd.parcel AS "חלקה",
  pd.area_sqm AS "שטח",
  fg.family_name AS "קבוצה"
FROM public.partnership_deals pd
LEFT JOIN public.family_groups fg ON fg.id = pd.family_group_id
WHERE pd.family_group_id = 'a990ed96-5b11-4d1a-9db1-89c76833af8b'
  AND pd.parcel = 14
  AND pd.is_active = TRUE;


-- ============================================================
-- שאילתה 2: deal_replaced_owners של עסקה זו (לפני מחיקה)
-- ============================================================
SELECT
  '🔍 לפני — קיזוזים פעילים' AS "שלב",
  dro.deal_id,
  dro.signed_owner_id,
  so.owner_name AS "בעל הקרקע המקוזז",
  pd.deal_name AS "שם העסקה"
FROM public.deal_replaced_owners dro
JOIN public.partnership_deals pd ON pd.id = dro.deal_id
LEFT JOIN public.signed_owners so ON so.id = dro.signed_owner_id
WHERE pd.family_group_id = 'a990ed96-5b11-4d1a-9db1-89c76833af8b'
  AND pd.parcel = 14;


-- ============================================================
-- שאילתה 3: שינוי A — הסרת העסקה מהקבוצה
-- ============================================================
UPDATE public.partnership_deals
SET family_group_id = NULL
WHERE family_group_id = 'a990ed96-5b11-4d1a-9db1-89c76833af8b'
  AND parcel = 14
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 4: שינוי B — מחיקת deal_replaced_owners של אותה עסקה
-- (משתמש בעובדה שעדיין יש פעולת UPDATE-Phase לאותה עסקה — כעת NULL)
-- ============================================================
DELETE FROM public.deal_replaced_owners
WHERE deal_id IN (
  SELECT pd.id
  FROM public.partnership_deals pd
  WHERE pd.parcel = 14
    AND pd.deal_name ILIKE '%יצחקי%'
    AND pd.is_active = TRUE
);


-- ============================================================
-- שאילתה 5: שינוי C1 — עדכון רוכשי וזוב מ-1,487 ל-1,987
-- ============================================================
UPDATE public.signed_owners
SET agreement_area = 1987
WHERE owner_name ILIKE '%רוכשי%וזוב%'
  AND parcel = 14
  AND family_group_id = 'a990ed96-5b11-4d1a-9db1-89c76833af8b'
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 6: שינוי C2 — עדכון דסטא ומחלבות מ-1,245 ל-995
-- ============================================================
UPDATE public.signed_owners
SET agreement_area = 995
WHERE id_number IN ('515133858', '512250143')  -- דסטא, מחלבות
  AND is_active = TRUE;


-- ============================================================
-- שאילתה 7: אימות אחרי — כל החתומים בקבוצת יצחקי
-- ============================================================
SELECT
  '✅ אחרי — חתומי קבוצת יצחקי' AS "שלב",
  s.owner_name AS "שם",
  s.id_number AS "ת.ז./ח.פ.",
  s.parcel AS "חלקה",
  s.agreement_area AS "שטח"
FROM public.signed_owners s
WHERE s.family_group_id = 'a990ed96-5b11-4d1a-9db1-89c76833af8b'
  AND s.is_active = TRUE
ORDER BY s.parcel, s.agreement_area DESC NULLS LAST;


-- ============================================================
-- שאילתה 8: אימות סופי — סך השטח בקבוצה
-- ============================================================
SELECT
  '🎯 סיכום סופי' AS "מדד",
  s.parcel AS "חלקה",
  COUNT(s.id) AS "מס׳ חתומים",
  SUM(s.agreement_area) AS "סך שטח"
FROM public.signed_owners s
WHERE s.family_group_id = 'a990ed96-5b11-4d1a-9db1-89c76833af8b'
  AND s.is_active = TRUE
GROUP BY s.parcel
ORDER BY s.parcel;


