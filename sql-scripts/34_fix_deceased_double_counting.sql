-- מטרה: תיקון כפילות ספירת שטח של בעלי קרקע ז"ל שהקרקע
-- שלהם כבר רשומה על שם היורשים ב-DB.
--
-- אבי הבהיר (12/05/2026 22:15):
--   "זלץ דניאלה מאירה נפטרה והקרקע שלה ממזמן רשומה על שם הילדים היורשים.
--    יהודה דניאלי נפטר והוריש את הקרקע."
--
-- זה אותו דפוס כמו עוצמה קונקשיין (4 רשומות ישנות) — צריך
-- לסמן את המנוחים כ-is_active=FALSE כדי למנוע ספירה כפולה.
--
-- בטוח להריץ פעמיים.
-- ============================================================


-- ============================================================
-- שלב 1 — מאירה דניאלה זלץ ז"ל (חלקות 28, 51)
-- ============================================================
-- כפילות עם 3 הבנים שכבר ב-DB:
--   058387713 זלץ ניר חיים (חלקות 28, 51)
--   025456807 קרון-זלץ שירה חנה (חלקות 28, 51)
--   022811897 זלץ גיל (חלקות 28, 51)
-- סה"כ 3 × 4,215 = 12,645 מ"ר = בדיוק השטח של מאירה
--
-- → המאירה היא היסטוריה. סימון is_active=FALSE.
UPDATE public.signed_owners
SET
  is_active = FALSE,
  legal_notes = COALESCE(legal_notes || ' | ', '') ||
                'תיקון 12/05/2026: רשומה זו של מאירה דניאלה זלץ ז"ל נספרה ' ||
                'בכפילות עם 3 בניה היורשים (זלץ ניר חיים, קרון-זלץ שירה חנה, זלץ גיל) ' ||
                'שכבר ב-DB עם שטח 3 × 4,215 = 12,645 מ"ר. ' ||
                'is_active=FALSE כדי למנוע ספירה כפולה. ההיסטוריה נשמרת.'
WHERE id_number = '000156293'
  AND owner_name LIKE '%זלץ%מאירה%'
  AND is_active = TRUE;


-- ============================================================
-- שלב 2 — בדיקה: האם יש יורשים של יהודה דניאלי ז"ל ב-DB?
-- ============================================================
-- נסח 67 מאי 2026: יהודה דניאלי (002787430) הוא הבעלים הרשום.
-- אבי אישר: "נפטר והוריש את הקרקע".
-- בודקים אם יורשים כבר ב-DB:

SELECT
  s.id_number,
  s.owner_name,
  s.parcel,
  s.agreement_area,
  s.master_2015_status,
  s.transition_type,
  s.predecessor_id_number
FROM public.signed_owners s
WHERE s.is_active = TRUE
  AND (
    s.predecessor_id_number = '002787430'
    OR s.predecessor_id_number = '000278743'
    OR s.owner_name ILIKE '%דניאלי%'
  )
ORDER BY s.owner_name, s.parcel;


-- ============================================================
-- שלב 3 — וידוא תיקון זלץ
-- ============================================================
-- צפי: לראות 4 רשומות זלץ
--   • 3 בנים פעילים (~4,215 מ"ר כל אחד)
--   • 1 מאירה ז"ל is_active=FALSE
SELECT
  id_number,
  owner_name,
  parcel,
  ROUND(agreement_area::numeric, 2) AS area,
  is_active,
  master_2015_status
FROM public.signed_owners
WHERE id_number IN ('000156293', '058387713', '025456807', '022811897')
ORDER BY owner_name, parcel;


-- ============================================================
-- שלב 4 — סיכום מחודש לפי קבוצה אחרי התיקון
-- ============================================================
WITH owner_areas AS (
  SELECT
    s.family_group_id,
    s.id_number,
    s.agreement_area * COALESCE(pm.participation_factor::numeric, 1) AS effective_area
  FROM public.signed_owners s
  LEFT JOIN public.parcels_meta pm ON pm.parcel = s.parcel
  WHERE s.is_active = TRUE
)
SELECT
  '🎯 סיכום אחרי תיקון' AS "מדד",
  COUNT(DISTINCT id_number) AS "בעלים_פעילים",
  ROUND(SUM(effective_area)::numeric, 0) AS "שטח_לאיחוד_מר",
  ROUND(SUM(effective_area)::numeric / 1000, 1) AS "שטח_לאיחוד_דונם"
FROM owner_areas;


