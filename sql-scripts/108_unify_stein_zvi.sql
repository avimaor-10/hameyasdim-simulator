-- מטרה: עדכון שטח של שטיין צבי בחלקה 19 ל-1,000 מ"ר (מקור: נסח).
--
-- רקע (אישור משתמש 17/05/2026 + נסח עדכני + נסח היסטורי):
--   • צבי ועליזה שטיין רכשו ביחד ב-02/12/1993 (שטר 27263/1993/41).
--     500 מ"ר כל אחד.
--   • שניהם חתמו על הסכם הניהול המקורי (2015).
--   • עליזה ז"ל (ת"ז 005137237) נפטרה. צבי ירש את חלקה דרך
--     צוואה ב-03/10/2018 (שטר 42181/2018/1).
--   • היום צבי מחזיק לבדו 1,000 מ"ר בחלקה 19.
--
-- 📐 חישוב מהנסח (חלקה 19 = 26,980 מ"ר):
--   • שטר 27263/1993/41: 500/26980 × 26980 = 500.000 מ"ר
--   • שטר 42181/2018/1:   25/1349  × 26980 = 500.000 מ"ר (1349×20=26980)
--   • סה"כ: 1,000.000 מ"ר בדיוק לפי הנסח
--
-- 🪞 כלל יסוד: "המידע הכי מדויק לגבי רישום שטחים נמצא בנסח הטאבו"
--
-- מצב ב-DB לפני:
--   • 005136957 (פורמט תקני) פעיל עם 500 מ"ר — חסרים 500 מ"ר ירושה
--   • 000513695 (פורמט ישן) כבר לא-פעיל — כפילות ידועה
--   • 005137237 (עליזה) לא ב-DB
--
-- מצב נדרש אחרי:
--   • 005136957 פעיל עם 1,000.000 מ"ר + קישור ירושה לעליזה + הערה
--   • 000513695 נשאר לא-פעיל + הערה מוסיפה תיעוד
--   • עליזה לא תיכנס ל-DB — רק תוזכר בהערות (זהה לאליעזר ונטורה)
--
-- 🔒 BEGIN ... COMMIT — בטוח להריצה.
-- ============================================================


BEGIN;


-- ============================================================
-- BEFORE: צילום מצב לפני
-- ============================================================
SELECT
  'BEFORE 108' AS step,
  s.id_number,
  s.owner_name,
  s.parcel,
  ROUND(s.agreement_area::numeric, 3)   AS agreement_area,
  ROUND(s.unification_area::numeric, 3) AS unification_area,
  s.is_active,
  s.is_signed,
  s.master_2015_status,
  s.inherited_from_id_number
FROM public.signed_owners s
WHERE s.id_number IN ('005136957', '000513695', '0513695', '5136957')
  AND s.parcel = 19
ORDER BY s.is_active DESC, s.id_number;


-- ============================================================
-- STEP A: עדכון שטיין צבי (005136957) ל-1,000.000 מ"ר
-- ============================================================
UPDATE public.signed_owners
SET
  agreement_area           = 1000.000,
  unification_area         = 1000.000,
  inherited_from_id_number = '005137237',
  master_2015_status       = 'verified',
  master_2015_notes        =
    'שטיין צבי ואשתו שטיין עליזה ז"ל (ת"ז 005137237) רכשו ביחד ' ||
    'ב-02/12/1993 (שטר 27263/1993/41), 500 מ"ר כל אחד = סה"כ 1,000 מ"ר. ' ||
    'שניהם חתמו על הסכם הניהול המקורי (2015). עליזה נפטרה וצבי ירש ' ||
    'את חלקה דרך צוואה ב-03/10/2018 (שטר 42181/2018/1, חלק 25/1349 ' ||
    '= 500 מ"ר). היום צבי מחזיק לבדו 1,000.000 מ"ר בחלקה 19 ' ||
    '(חישוב מדויק מהנסח: 500 רכישה + 500 ירושה). ' ||
    'עליזה לא רשומה ב-DB כיוון שהירושה כבר נרשמה. רשומה כפילה ' ||
    '000513695 (פורמט ת"ז ישן) כבר לא-פעילה. ' ||
    'תיעוד היסטורי 17/05/2026 (סקריפט 108).'
WHERE id_number = '005136957'
  AND parcel = 19;


-- ============================================================
-- STEP B: הוספת הערה ל-000513695 (כבר FALSE)
-- ============================================================
UPDATE public.signed_owners
SET master_2015_notes = COALESCE(master_2015_notes, '') ||
  CASE WHEN COALESCE(master_2015_notes, '') = '' THEN '' ELSE ' | ' END ||
  'הוסר 17/05/2026 (סקריפט 108): כפילות פורמט ת"ז של שטיין צבי. ' ||
  'הפורמט הישן 0513695 (7 ספרות מהנסח 1993) הומר לפורמט תקני ' ||
  '005136957 (9 ספרות). הרשומה הפעילה היחידה היא 005136957 ' ||
  'עם שטח כולל 1,000 מ"ר (כולל ירושה מעליזה ז"ל).'
WHERE id_number = '000513695'
  AND parcel = 19
  AND is_active = FALSE;


-- ============================================================
-- AFTER: וידוא התיקון
-- ============================================================
SELECT
  'AFTER 108' AS step,
  s.id_number,
  s.owner_name,
  s.parcel,
  ROUND(s.agreement_area::numeric, 3)   AS agreement_area,
  ROUND(s.unification_area::numeric, 3) AS unification_area,
  s.is_active,
  s.is_signed,
  s.master_2015_status,
  s.inherited_from_id_number,
  COALESCE(LEFT(s.master_2015_notes, 100), '') AS notes
FROM public.signed_owners s
WHERE s.id_number IN ('005136957', '000513695')
  AND s.parcel = 19
ORDER BY s.is_active DESC, s.id_number;


-- ============================================================
-- SUMMARY: סיכום סופי לשטיין צבי בחלקה 19
-- ============================================================
SELECT
  'FINAL COUNT' AS step,
  COUNT(*) FILTER (WHERE is_active = TRUE)  AS active_records,
  COUNT(*) FILTER (WHERE is_active = FALSE) AS inactive_records,
  ROUND(SUM(agreement_area) FILTER (WHERE is_active = TRUE)::numeric, 3)
    AS total_active_area
FROM public.signed_owners
WHERE id_number IN ('005136957', '000513695')
  AND parcel = 19;


COMMIT;


