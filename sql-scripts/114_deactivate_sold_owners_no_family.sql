--
-- 🪞 כלל יסוד (אישור משתמש 17/05 ערב):
-- "ילדי מועלם לא דומים למקרה זה, מכוון שילדי מועלם מתחברים למשפחה
--  קיימת שיש לה עוד קרקע ומהווים קבוצה להקצאה, אולם הרשימה בבן גרא
--  ומטה — אין להם עוד קרקע של משפחה ולכן חוץ מהידיעה שהם מכרו
--  אין להם כל החשיבות להופיע ברשימה."
--
-- 📐 6 הבעלים:
--   • בן גרא עפרה (000156180, חלקה 17) — מכרה לעסקת שותפות "כוכי בן גרא"
--   • צלליכין רוחמה (000156179, חלקה 17) — מכרה לעסקת שותפות "צלליכין"
--   • ספיבק דינה (017283631, חלקה 44) — מכרה ישירות לשחר בר/גולנדר/אנה בקר
--   • ספיבק ראובן (017283623, חלקה 44) — מכר ישירות לשחר בר/גולנדר/אנה בקר
--   • פרסטנפלד יהודה (007454333, חלקה 44) — מכר ישירות
--   • פרסטנפלד תמר (000435339, חלקה 44) — מכרה ישירות
--
-- אף אחד מהם לא חלק ממשפחה שמחזיקה עוד קרקע — אין רלוונטיות לקבוצה.
--
-- 🔒 BEGIN ... COMMIT — בטוח להריצה.
-- 🛡 הקישור ל-deal_replaced_owners של בן גרא וצלליכין נשמר (תיעוד היסטורי).
-- ============================================================


BEGIN;


-- BEFORE
SELECT 'BEFORE 114' AS step,
  s.id_number, s.owner_name, s.parcel,
  ROUND(s.agreement_area::numeric, 3) AS agreement_area,
  ROUND(s.unification_area::numeric, 3) AS unification_area,
  s.is_active, s.ownership_category
FROM public.signed_owners s
WHERE s.id_number IN (
  '000156180', '000156179',
  '017283631', '017283623',
  '007454333', '000435339'
)
ORDER BY s.parcel, s.owner_name;


-- STEP A: סימון is_active=FALSE לכל ה-6
UPDATE public.signed_owners
SET is_active = FALSE,
    master_2015_notes = COALESCE(master_2015_notes, '') ||
      CASE WHEN COALESCE(master_2015_notes, '') = '' THEN '' ELSE ' | ' END ||
      $note$סומן is_active=FALSE 17/05/2026 (סקריפט 114): הבעלים מכר את כל חלקו ואין משפחה שממשיכה להחזיק קרקע. חוץ מהידיעה ההיסטורית שמכר — אין רלוונטיות להציג ברשימת בעלי הקרקע הפעילים. הקישור ל-deal_replaced_owners (אם קיים) נשמר לתיעוד היסטורי. הכלל: בעלים שמכרו ויש משפחה שממשיכה (כמו ילדי מועלם) → נשארים פעילים; בעלים שמכרו ואין משפחה → FALSE.$note$
WHERE id_number IN (
  '000156180', '000156179',
  '017283631', '017283623',
  '007454333', '000435339'
);


-- AFTER
SELECT 'AFTER 114' AS step,
  s.id_number, s.owner_name, s.parcel,
  ROUND(s.agreement_area::numeric, 3) AS agreement_area,
  ROUND(s.unification_area::numeric, 3) AS unification_area,
  s.is_active, s.ownership_category
FROM public.signed_owners s
WHERE s.id_number IN (
  '000156180', '000156179',
  '017283631', '017283623',
  '007454333', '000435339'
)
ORDER BY s.parcel, s.owner_name;


COMMIT;


