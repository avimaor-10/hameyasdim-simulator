-- ============================================================
-- שאילתה 1: כל הרשומות עם ת.ז. שמכילה 755793
-- ============================================================
SELECT
  s.id_number      AS "ת.ז.",
  s.owner_name     AS "שם",
  s.parcel         AS "חלקה",
  ROUND(s.agreement_area::numeric, 2) AS "שטח",
  s.is_active      AS "פעיל?",
  s.is_signed      AS "חתום?",
  COALESCE(LEFT(s.master_2015_notes, 80), '') AS "הערה (80 תווים)"
FROM public.signed_owners s
WHERE s.id_number LIKE '%755793%'
   OR s.owner_name ILIKE '%מועלם%אליהו%'
ORDER BY s.parcel, s.is_active DESC, s.id_number;


-- ============================================================
-- שאילתה 2: ספירה לפי is_active עבור 000755793
-- ============================================================
SELECT
  '000755793 בלבד' AS "פילטר",
  COUNT(*) FILTER (WHERE is_active = TRUE)  AS "פעיל",
  COUNT(*) FILTER (WHERE is_active = FALSE) AS "לא פעיל",
  COUNT(*) AS "סה״כ"
FROM public.signed_owners
WHERE id_number = '000755793';


-- ============================================================
-- שאילתה 3: ספירה לפי is_active עבור 4/755793
-- ============================================================
SELECT
  '4/755793 בלבד' AS "פילטר",
  COUNT(*) FILTER (WHERE is_active = TRUE)  AS "פעיל",
  COUNT(*) FILTER (WHERE is_active = FALSE) AS "לא פעיל",
  COUNT(*) AS "סה״כ"
FROM public.signed_owners
WHERE id_number = '4/755793';


-- ============================================================
-- שאילתה 4: מצב כללי של משפחת מועלם-מור (כל הפעילים)
-- ============================================================
SELECT
  s.id_number      AS "ת.ז.",
  s.owner_name     AS "שם",
  STRING_AGG(s.parcel::text, ',' ORDER BY s.parcel) AS "חלקות",
  COUNT(*)         AS "מספר רשומות",
  ROUND(SUM(s.agreement_area)::numeric, 2) AS "סה״כ שטח"
FROM public.signed_owners s
WHERE s.family_group_id = (
        SELECT id FROM public.family_groups WHERE family_name = 'משפחת מועלם - מור' LIMIT 1
      )
  AND s.is_active = TRUE
GROUP BY s.id_number, s.owner_name
ORDER BY s.owner_name;


