-- 🎯 מטרה: הפיכת ה-UPDATE של סקריפט 98 — להחזיר את 17 הרשומות
--          חזרה ל-'verified' עם הערה משופרת.
--
-- 🔍 רקע: המשתמש אישר שדניאלי סמדר חתומה בפועל למרות שהאקסל
--         מסמן אותה flag=0. כלומר האקסל לא מהימן לכל הרשומות,
--         ויש להחזיר את כולן ל-verified עם הערה לבדיקה ידנית.
--
-- 📐 לוגיקה:
--   • מסנן רק רשומות שסומנו 'not_verified' ע"י סקריפט 98
--     (זוהוי לפי ההערה "(לא חתם)")
--   • מחזיר master_2015_status ל-'verified'
--   • מעדכן ההערה: "(לא חתם)" → "(אקסל מסמן לא חתם - לבדיקה ידנית)"
--
-- 🔒 לא נוגעים ברשומות אחרות:
--   • not_verified אמיתי קיים → נשאר
--   • verified קיים → נשאר
--   • NULL → נשאר
-- ============================================================


BEGIN;


-- ============================================================
-- שאילתה 1: לפני - הרשומות שיוחזרו
-- ============================================================
SELECT '📸 לפני 99' AS "שלב",
  COUNT(*) AS "כמה ישתנו"
FROM public.signed_owners
WHERE is_active = TRUE
  AND master_2015_status = 'not_verified'
  AND master_2015_notes LIKE '%(לא חתם)%'
  AND master_2015_notes NOT LIKE '%לבדיקה%';


-- ============================================================
-- צעד A: UPDATE — not_verified → verified
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'verified',
  master_2015_notes  = REPLACE(master_2015_notes,
                               '(לא חתם)',
                               '(אקסל מסמן לא חתם - לבדיקה ידנית)')
WHERE is_active = TRUE
  AND master_2015_status = 'not_verified'
  AND master_2015_notes LIKE '%(לא חתם)%'
  AND master_2015_notes NOT LIKE '%לבדיקה%';


-- ============================================================
-- שאילתה 2: אחרי - פיזור master_2015_status
-- ============================================================
SELECT '✅ אחרי 99' AS "שלב",
  COALESCE(master_2015_status, '(NULL)') AS "סטטוס",
  COUNT(*) AS "כמות"
FROM public.signed_owners
WHERE is_active = TRUE
GROUP BY master_2015_status
ORDER BY COUNT(*) DESC;


-- ============================================================
-- שאילתה 3: רשימת ה-17 שהוחזרו (לאמת)
-- ============================================================
SELECT
  owner_name                    AS "שם ב-DB",
  id_number                     AS "ת״ז",
  parcel                        AS "חלקה",
  master_2015_status            AS "סטטוס",
  LEFT(master_2015_notes, 90)   AS "הערה (90 תווים)"
FROM public.signed_owners
WHERE is_active = TRUE
  AND master_2015_status = 'verified'
  AND master_2015_notes LIKE '%לבדיקה ידנית%'
ORDER BY owner_name;


COMMIT;


