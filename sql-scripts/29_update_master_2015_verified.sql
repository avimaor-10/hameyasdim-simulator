-- מטרה: עדכון `master_2015_status='verified'` ל-5 בעלים שזוהו
-- ב-DB עם סדר שמות הפוך — לפי תוצאות סקריפט 28.
--
-- הסדר ההפוך (מאסטר → DB):
--   1. בן בסט טל → טל בן בסט         ת"ז 055624563
--   2. בן חורין נורית → נורית בן חורין ת"ז 050259704
--   3. בר שחר → שחר בר               ת"ז 033045139 (מאומת בנסח חלקה 44)
--   4. גולנדר אביאל → אביאל גולנדר    ת"ז 033593948 (מאומת בנסח חלקה 44)
--   5. שרוני ענבר מרים → ענבר שרוני   ת"ז 022125090 (לפי זיכרון 07/05/2026)
--
-- בטוח להריץ פעמיים — UPDATE עם WHERE מדויק.
-- ============================================================

-- 5 בעלים שזוהו כחתומי מקור 2015 (סדר שמות הפוך)
UPDATE public.signed_owners
SET master_2015_status = 'verified'
WHERE id_number IN (
  '055624563',  -- טל בן בסט (במאסטר: בן בסט טל)
  '050259704',  -- נורית בן חורין (במאסטר: בן חורין נורית)
  '033045139',  -- שחר בר (במאסטר: בר שחר)
  '033593948',  -- אביאל גולנדר (במאסטר: גולנדר אביאל)
  '022125090'   -- ענבר שרוני (במאסטר: שרוני ענבר מרים)
)
AND (master_2015_status IS NULL OR master_2015_status != 'verified');

-- אימות
SELECT
  id_number,
  owner_name,
  master_2015_status,
  ownership_category,
  is_active
FROM public.signed_owners
WHERE id_number IN ('055624563', '050259704', '033045139', '033593948', '022125090')
ORDER BY id_number;


-- ============================================================
-- בנוסף: 4 שמות שצריכים קישור ל-family_group_id קיים
-- ============================================================
-- (לפי סקריפט 28: בקבוצת משפחה אבל לא מקושרים ישירות)

-- בן גרא עפרה — אם קיימת ב-DB, לקשר ל"משפחת דרומי/בן דוד"
-- גולדמן משה+תחיה — אם קיימים ב-DB, לקשר ל"משפחת גולדמן"
-- הימן תמר — אם קיימת ב-DB, לקשר ל"משפחת הימן"
--
-- אבל לפי סקריפט 28, הם לא נמצאו ב-signed_owners בכלל
-- (רק הקבוצה קיימת, לא הבעלים) — לכן הם חסרים אמיתיים,
-- וצריך להוסיף אותם עם family_group_id מתאים.
--
-- → ראה סקריפט 30: הוספת חסרים אמיתיים


-- ============================================================
-- שאילתת אימות סופית: כמה חתומי 2015 מאומתים יש עכשיו
-- ============================================================
SELECT
  master_2015_status,
  COUNT(DISTINCT id_number) AS unique_owners,
  COUNT(*) AS records
FROM public.signed_owners
WHERE is_active = TRUE
GROUP BY master_2015_status
ORDER BY master_2015_status;
