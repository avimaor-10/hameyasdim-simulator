-- ============================================================
-- אימות אחרי — מצב סופי
-- ============================================================
SELECT
  '✅ אחרי התיקון' AS "שלב",
  s.owner_name AS "שם",
  s.id_number AS "ת_ז",
  s.parcel AS "חלקה",
  ROUND(s.agreement_area::numeric, 2) AS "שטח",
  s.is_active AS "פעיל",
  fg.family_name AS "קבוצה"
FROM public.signed_owners s
LEFT JOIN public.family_groups fg ON fg.id = s.family_group_id
WHERE s.id_number IN (
  '009252818', '000513695', '005136957',
  '038152104', '021775598',
  '017258823', '017258815',
  '005291626', '005491126',
  '005319864', '004222300',
  '005284319'
)
ORDER BY fg.family_name NULLS LAST, s.owner_name, s.parcel;


-- ============================================================
-- אימות יצירת 3 קבוצות חדשות
-- ============================================================
SELECT
  '👨‍👩‍👧 קבוצות חדשות שנוצרו' AS "מדד",
  fg.family_name AS "קבוצה",
  fg.is_active AS "פעיל",
  (SELECT COUNT(*) FROM public.signed_owners s
   WHERE s.family_group_id = fg.id AND s.is_active = TRUE) AS "חברים_פעילים",
  (SELECT STRING_AGG(s.owner_name, ' + ' ORDER BY s.owner_name)
   FROM public.signed_owners s
   WHERE s.family_group_id = fg.id) AS "שמות_חברים"
FROM public.family_groups fg
WHERE fg.family_name IN ('משפחת ליפ', 'משפחת בוקשפן', 'משפחת אשר', 'משפחת גבאי')
ORDER BY fg.family_name;


