-- לוגיקת התאמה משופרת — מתאים גם אם שם המשפחה במקום שני
match_status AS (
  SELECT
    mp.master_name,
    mp.parcel,
    CASE
      WHEN EXISTS (
        SELECT 1 FROM public.signed_owners s
        WHERE s.parcel = mp.parcel
          AND s.is_active = TRUE
          AND (
            -- התאמה למילה ראשונה (לדוגמה: "בן דוד מרב" ↔ "בן דוד מרב")
            SPLIT_PART(s.owner_name, ' ', 1) = SPLIT_PART(mp.master_name, ' ', 1)
            -- התאמה ל-2 מילים ראשונות (לדוגמה: "בן דוד")
            OR s.owner_name ILIKE SPLIT_PART(mp.master_name, ' ', 1) || ' ' || SPLIT_PART(mp.master_name, ' ', 2) || '%'
            -- התאמה הפוכה (שם פרטי תחילה)
            OR s.owner_name ILIKE '%' || SPLIT_PART(mp.master_name, ' ', 1) || '%' || SPLIT_PART(mp.master_name, ' ', 2) || '%'
            OR s.owner_name ILIKE '%' || SPLIT_PART(mp.master_name, ' ', 2) || '%' || SPLIT_PART(mp.master_name, ' ', 1) || '%'
          )
      ) THEN 'matched'
      ELSE 'missing'
    END AS status
  FROM master_pairs mp
)
SELECT
  '📊 סה"כ בעלים מאסטר ייחודיים (v2)' AS "מדד",
