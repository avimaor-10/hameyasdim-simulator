-- מטרה: תיקון משפחת שראל בחלקה 19 לפי הנסח, וקישור 2 הבעלים
--        לקבוצת משפחה חדשה "משפחת שראל".
--
-- רקע (חקירת נסח עדכני + נסח היסטורי + נספח א' + אישור משתמש):
--
-- 📜 דור ראשון (רכישה 1993, שטר 27263/1993/9 - בוטל):
--   • שראל ישראל (ת"ז 6534357) — 250 מ"ר
--   • שראל רעננה (ת"ז 6544454) — 250 מ"ר
--   ישראל ורעננה (כנראה הורי חנן) העבירו ב-2005 ללא תמורה.
--   אינם רשומים ב-DB — מוזכרים רק בהערות לתיעוד היסטוריה.
--
-- 📜 דור שני (העברה ללא תמורה 21/07/2005, שטר 24792/2005/1):
--   • שראל חנן (ת"ז 022305510) — 250 מ"ר ← קיבל מאביו
--   • שראל גלטשטיין בטינה (ת"ז 015859036) — 250 מ"ר ← קיבלה מאמו
--
-- ✍ ב-2016 שניהם חתמו על הסכם הניהול.
--
-- 📐 חישוב מהנסח (חלקה 19 = 26,980 מ"ר):
--   • שראל חנן: 250/26980 × 26980 = 250.000 מ"ר
--   • בטינה: 250/26980 × 26980 = 250.000 מ"ר
--   • סה"כ למשפחה: 500.000 מ"ר
--
-- מצב ב-DB לפני:
--   • שראל חנן (022305510) — 250.000 מ"ר ✅ תקין
--   • בטינה (015859036) — 499.580 מ"ר ❌ כפול ממה שצריך
--   • family_group_id של שניהם: NULL ❌ לא מקושרים
--   • master_2015_status של שניהם: not_checked ❌ למרות שחתמו ב-2016
--
-- מצב נדרש אחרי:
--   • בטינה → 250.000 מ"ר (תיקון לפי הנסח)
--   • שניהם → family_group_id של "משפחת שראל" (חדש)
--   • שניהם → master_2015_status = 'verified' (חתמו ב-2016)
--   • שניהם → הערה היסטורית מלאה
--
-- 🔒 BEGIN ... COMMIT — בטוח להריצה.
-- ============================================================


BEGIN;


-- ============================================================
-- BEFORE: צילום מצב לפני
-- ============================================================
SELECT
  'BEFORE 110' AS step,
  s.id_number,
  s.owner_name,
  s.parcel,
  ROUND(s.agreement_area::numeric, 3)   AS agreement_area,
  ROUND(s.unification_area::numeric, 3) AS unification_area,
  s.is_active,
  s.master_2015_status,
  s.family_group_id
FROM public.signed_owners s
WHERE s.id_number IN ('022305510', '015859036')
  AND s.parcel = 19
ORDER BY s.id_number;


-- ============================================================
-- STEP A: יצירת family_group "משפחת שראל" (אם לא קיים)
-- ============================================================
INSERT INTO public.family_groups (family_name, is_active)
SELECT 'משפחת שראל', TRUE
WHERE NOT EXISTS (
  SELECT 1 FROM public.family_groups
  WHERE family_name = 'משפחת שראל'
);


-- ============================================================
-- STEP B: עדכון שראל גלטשטיין בטינה (015859036)
-- ============================================================
UPDATE public.signed_owners
SET
  agreement_area     = 250.000,
  unification_area   = 250.000,
  master_2015_status = 'verified',
  family_group_id    = (SELECT id FROM public.family_groups WHERE family_name = 'משפחת שראל'),
  master_2015_notes  =
    'שראל גלטשטיין בטינה ושראל חנן (בעלה) הם זוג נשוי בעלי קרקע בחלקה 19. ' ||
    'קיבלו ב-21/07/2005 (שטר 24792/2005/1) מכר ללא תמורה מהורי חנן: ' ||
    'שראל ישראל ז"ל/חי (ת"ז 6534357) ושראל רעננה ז"ל/חית (ת"ז 6544454), ' ||
    'שרכשו במקור ב-02/12/1993 (שטר 27263/1993/9). ' ||
    'בטינה ירשה 250 מ"ר. שמה המלא: בטינה יהודית גלטשטיין שראל. ' ||
    'שניהם חתמו על הסכם הניהול ב-2016. ' ||
    'תיקון 17/05/2026 (סקריפט 110): השטח עודכן מ-499.580 ל-250.000 לפי הנסח. ' ||
    'הכלל: נסח טאבו = מקור 100% לרישום שטחים.'
WHERE id_number = '015859036'
  AND parcel = 19;


-- ============================================================
-- STEP C: עדכון שראל חנן (022305510) — שטח כבר תקין, רק לקשר ולעדכן סטטוס
-- ============================================================
UPDATE public.signed_owners
SET
  master_2015_status = 'verified',
  family_group_id    = (SELECT id FROM public.family_groups WHERE family_name = 'משפחת שראל'),
  master_2015_notes  =
    'שראל חנן ושראל גלטשטיין בטינה (אשתו) הם זוג נשוי בעלי קרקע בחלקה 19. ' ||
    'קיבלו ב-21/07/2005 (שטר 24792/2005/1) מכר ללא תמורה מהורי חנן: ' ||
    'שראל ישראל (ת"ז 6534357) ושראל רעננה (ת"ז 6544454), ' ||
    'שרכשו במקור ב-02/12/1993 (שטר 27263/1993/9). ' ||
    'חנן ירש 250 מ"ר. שניהם חתמו על הסכם הניהול ב-2016. ' ||
    'תיעוד 17/05/2026 (סקריפט 110): קישור למשפחת שראל + סטטוס verified.'
WHERE id_number = '022305510'
  AND parcel = 19;


-- ============================================================
-- AFTER: וידוא התיקון
-- ============================================================
SELECT
  'AFTER 110' AS step,
  s.id_number,
  s.owner_name,
  s.parcel,
  ROUND(s.agreement_area::numeric, 3)   AS agreement_area,
  ROUND(s.unification_area::numeric, 3) AS unification_area,
  s.is_active,
  s.master_2015_status,
  s.family_group_id,
  fg.family_name
FROM public.signed_owners s
LEFT JOIN public.family_groups fg ON fg.id = s.family_group_id
WHERE s.id_number IN ('022305510', '015859036')
  AND s.parcel = 19
ORDER BY s.id_number;


-- ============================================================
-- SUMMARY: סיכום משפחת שראל
-- ============================================================
SELECT
  'FAMILY SUMMARY' AS step,
  COUNT(*) AS members,
  ROUND(SUM(s.agreement_area)::numeric, 3) AS total_area,
  STRING_AGG(s.owner_name, ', ' ORDER BY s.owner_name) AS owners
FROM public.signed_owners s
JOIN public.family_groups fg ON fg.id = s.family_group_id
WHERE fg.family_name = 'משפחת שראל'
  AND s.is_active = TRUE;


COMMIT;


