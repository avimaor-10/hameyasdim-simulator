-- מטרה: תיקון רטרואקטיבי של שטח ונטורה רחל בחלקה 19 לפי הנסח.
--
-- רקע: סקריפט 107 (17/05/2026 בוקר) הציב 501.828 מ"ר כי המשתמש
--   נתן את המספר ממנדיי. בדיעבד התברר שזה לא מדויק.
--
-- 🪞 כלל יסוד שנקבע: "המידע הכי מדויק לגבי רישום שטחים נמצא בנסח".
--
-- 📐 חישוב מדויק מהנסח (חלקה 19 = 26,980 מ"ר):
--   • שטר 27263/1993/21: 250/26980 × 26980 = 250.000 מ"ר (רכישה 1993)
--   • שטר 7994/2022/1:    25/2698 × 26980 = 250.000 מ"ר (ירושה 2022)
--     (2698 × 10 = 26980 → 25/2698 = 250/26980)
--   • סה"כ: 500.000 מ"ר בדיוק
--
-- מצב לפני: 501.828 מ"ר (ממנדיי, שגוי)
-- מצב נדרש: 500.000 מ"ר (לפי הנסח, מדויק)
--
-- 🔒 BEGIN ... COMMIT — בטוח להריצה. רק UPDATE על שדה שטח.
-- ============================================================


BEGIN;


-- ============================================================
-- BEFORE: צילום מצב לפני
-- ============================================================
SELECT
  'BEFORE 109' AS step,
  s.id_number,
  s.owner_name,
  s.parcel,
  ROUND(s.agreement_area::numeric, 3)   AS agreement_area,
  ROUND(s.unification_area::numeric, 3) AS unification_area,
  s.is_active,
  s.master_2015_status,
  s.inherited_from_id_number
FROM public.signed_owners s
WHERE s.id_number = '051551323'
  AND s.parcel = 19
  AND s.is_active = TRUE;


-- ============================================================
-- STEP A: תיקון השטח ל-500.000 לפי הנסח
-- ============================================================
UPDATE public.signed_owners
SET
  agreement_area    = 500.000,
  unification_area  = 500.000,
  master_2015_notes = master_2015_notes ||
    ' | תיקון 17/05/2026 (סקריפט 109): השטח עודכן מ-501.828 ל-500.000 ' ||
    'לפי הנסח. החישוב המדויק: 250/26980 (רכישה 1993, שטר 27263/1993/21) + ' ||
    '25/2698 (ירושה 2022, שטר 7994/2022/1) = 250 + 250 = 500.000 מ"ר. ' ||
    'המספר הקודם 501.828 הגיע ממנדיי ולא היה מדויק. ' ||
    'הכלל: נסח טאבו = מקור 100% למדידת שטחים.'
WHERE id_number = '051551323'
  AND parcel = 19
  AND is_active = TRUE;


-- ============================================================
-- AFTER: וידוא התיקון
-- ============================================================
SELECT
  'AFTER 109' AS step,
  s.id_number,
  s.owner_name,
  s.parcel,
  ROUND(s.agreement_area::numeric, 3)   AS agreement_area,
  ROUND(s.unification_area::numeric, 3) AS unification_area,
  s.is_active,
  s.master_2015_status,
  s.inherited_from_id_number,
  COALESCE(LEFT(s.master_2015_notes, 150), '') AS notes_preview
FROM public.signed_owners s
WHERE s.id_number = '051551323'
  AND s.parcel = 19
  AND s.is_active = TRUE;


COMMIT;


-- ============================================================
