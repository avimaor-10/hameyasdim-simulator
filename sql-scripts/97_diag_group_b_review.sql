-- 🎯 מטרה: סקריפט אבחנתי בלבד (SELECT) - לראות את ה-16
--          רשומות מ-Group B (לא חתמו באקסל אבל ב-DB).
--          על סמך זה נחליט מי באמת צריך 'not_verified' ומי
--          להישאר 'verified' (כי הוא יורש לגיטימי).
--
-- 📐 לוגיקה: מצא רשומות שסומנו ע"י סקריפט 96 עם הערה
--          "(לא חתם - דרך שרשור)" - הם המועמדים להורדה.
-- ============================================================


SELECT
  s.owner_name                              AS "שם ב-DB",
  s.id_number                               AS "ת״ז",
  s.parcel                                  AS "חלקה",
  s.master_2015_status                      AS "סטטוס נוכחי",
  s.master_2015_notes                       AS "הערה מלאה",
  s.inherited_from_id_number                          AS "יורש של (אם תועד)",
  CASE
    WHEN s.master_2015_notes LIKE '%(לא חתם - דרך שרשור)%'
     AND s.inherited_from_id_number IS NULL
    THEN '⚠ אולי צריך not_verified'

    WHEN s.master_2015_notes LIKE '%(לא חתם - דרך שרשור)%'
     AND s.inherited_from_id_number IS NOT NULL
    THEN '✅ verified (יורש מתועד)'

    WHEN s.master_2015_notes LIKE '%יורש%' OR s.master_2015_notes LIKE '%נכנס%בנעלי%'
    THEN '✅ verified (יורש בהערה)'

    ELSE '? לבדיקה ידנית'
  END                                       AS "המלצה"
FROM public.signed_owners s
WHERE s.is_active = TRUE
  AND s.master_2015_notes LIKE '%(לא חתם - דרך שרשור)%'
ORDER BY s.owner_name;


-- ============================================================
-- 📋 הסבר עמודות:
--   • "סטטוס נוכחי" - מה רשום עכשיו (אחרי 96)
--   • "הערה מלאה" - מה הסקריפט הוסיף + הערות קודמות
--   • "יורש של" - שדה inherited_from (אם מולא בעבר ע"י סקריפט 81)
--   • "המלצה" - האם להוריד ל-not_verified או להשאיר
--
-- 🔄 הצעד הבא: על סמך התוצאות -
--   • אם רוב הרשומות "✅ verified (יורש מתועד)" → לא עושים כלום
--   • אם יש "⚠ אולי צריך not_verified" → סקריפט 98 שמתקן רק אותן
-- ============================================================
