-- כל קבוצה מקבלת צבע מפלטה של 16 צבעים מובהקים
-- (תואמים תצוגה על רקע תצלום אוויר/לוויין).
--
-- שני קטגוריות העסקאות הקבועות (נדל"ן נדל"ן, דודי יצחקי) מקבלות
-- צבעים קבועים בקוד הצד-לקוח ולא דרך הטבלה הזו.
-- ============================================================

-- 1. הוספת עמודת color (אם עדיין לא קיימת)
ALTER TABLE public.family_groups
  ADD COLUMN IF NOT EXISTS color TEXT NOT NULL DEFAULT '#94a3b8';

-- 2. הקצאה אוטומטית של צבעים מהפלטה לקבוצות קיימות
--    (לפי סדר אלפבתי של family_name; מודולו 16 לאיחוד אוטומטי אם יש מעל 16 קבוצות)
WITH palette AS (
  SELECT * FROM (VALUES
    (1,  '#dc2626'),  -- אדום
    (2,  '#ea580c'),  -- כתום
    (3,  '#facc15'),  -- צהוב
    (4,  '#84cc16'),  -- ליים
    (5,  '#16a34a'),  -- ירוק
    (6,  '#0d9488'),  -- טורקיז
    (7,  '#0891b2'),  -- ציאן
    (8,  '#2563eb'),  -- כחול
    (9,  '#4f46e5'),  -- אינדיגו
    (10, '#7c3aed'),  -- סגול-כחול
    (11, '#9333ea'),  -- סגול
    (12, '#c026d3'),  -- פוקסיה
    (13, '#db2777'),  -- ורוד
    (14, '#f43f5e'),  -- רוז
    (15, '#92400e'),  -- חום
    (16, '#475569')   -- אפור-כחול
  ) AS t(idx, hex)
),
ranked AS (
  SELECT id, ROW_NUMBER() OVER (ORDER BY family_name) AS rn
  FROM public.family_groups
  WHERE is_active = TRUE
)
UPDATE public.family_groups fg
SET color = palette.hex
FROM ranked r
JOIN palette ON palette.idx = ((r.rn - 1) % 16) + 1
WHERE fg.id = r.id
  AND fg.color = '#94a3b8';   -- רק עדיין-לא-מוגדרים (ברירת מחדל)

-- 3. אימות — להציג את כל הקבוצות עם הצבעים שהוקצו
SELECT family_name, color, notes
FROM public.family_groups
WHERE is_active = TRUE
ORDER BY family_name;

